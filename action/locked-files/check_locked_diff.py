#!/usr/bin/env python3
"""Fail a PR when its diff touches code protected by @AILocked.

Reads every ``.vibetags-locks`` report under the working directory (JSON Lines
between ``# VIBETAGS`` hash markers, regenerated by the build step that runs
before this script), parses ``git diff`` against the merge base, and reports a
violation when:

  1. a changed line range intersects a locked element's line range,
  2. a removed line contains the ``@AILocked`` annotation itself (lock stripping),
  3. a deleted file contained ``@AILocked`` at the base revision.

Environment:
  VIBETAGS_BASE_REF    ref/SHA to diff against (required)
  VIBETAGS_WARN_ONLY   'true' to emit warnings instead of failing (default 'false')

Exit code 1 on violations unless warn-only.
"""

import json
import os
import re
import subprocess
import sys

HUNK_RE = re.compile(r"^@@ -(\d+)(?:,(\d+))? \+(\d+)(?:,(\d+))? @@")


def run_git(*args):
    result = subprocess.run(
        ["git", *args], capture_output=True, text=True, encoding="utf-8", errors="replace"
    )
    if result.returncode != 0:
        sys.stderr.write(result.stderr)
        raise SystemExit(f"git {' '.join(args)} failed with exit code {result.returncode}")
    return result.stdout


def find_reports(root):
    reports = []
    for dirpath, dirnames, filenames in os.walk(root):
        dirnames[:] = [d for d in dirnames if d not in (".git", "node_modules", "target", "build")]
        if ".vibetags-locks" in filenames:
            reports.append(os.path.join(dirpath, ".vibetags-locks"))
    return reports


def load_locks(report_paths, repo_root):
    """Returns a list of lock dicts with a normalized, repo-relative-ish 'file' path."""
    locks = []
    for report in report_paths:
        with open(report, encoding="utf-8") as fh:
            for line in fh:
                line = line.strip()
                if not line or line.startswith("#"):
                    continue  # markers, header comments, module sub-markers
                try:
                    entry = json.loads(line)
                except json.JSONDecodeError:
                    continue
                if entry.get("type") != "locked":
                    continue
                path = entry.get("file")
                if path:
                    path = path.replace("\\", "/")
                    rel = os.path.relpath(path, repo_root).replace("\\", "/")
                    if not rel.startswith(".."):
                        path = rel
                    entry["file"] = path
                locks.append(entry)
    return locks


def locks_for(locks, diff_path):
    """Locks whose file matches a diff path; tolerates absolute vs relative prefixes."""
    diff_path = diff_path.replace("\\", "/")
    matched = []
    for lock in locks:
        lock_path = lock.get("file")
        if not lock_path:
            continue
        if (
            lock_path == diff_path
            or lock_path.endswith("/" + diff_path)
            or diff_path.endswith("/" + lock_path)
        ):
            matched.append(lock)
    return matched


def parse_diff(diff_text):
    """Yields (path, old_path, hunks, removed_lines) per changed file.

    path is None for deleted files; each hunk is (new_start, new_count).
    removed_lines is the list of '-' line bodies across all hunks of the file.
    """
    path = old_path = None
    hunks, removed = [], []
    for line in diff_text.splitlines():
        if line.startswith("diff --git "):
            if old_path is not None or path is not None:
                yield path, old_path, hunks, removed
            path = old_path = None
            hunks, removed = [], []
        elif line.startswith("--- "):
            target = line[4:]
            old_path = None if target == "/dev/null" else target[2:]  # strip "a/"
        elif line.startswith("+++ "):
            target = line[4:]
            path = None if target == "/dev/null" else target[2:]  # strip "b/"
        elif line.startswith("@@"):
            m = HUNK_RE.match(line)
            if m:
                new_start = int(m.group(3))
                new_count = int(m.group(4)) if m.group(4) is not None else 1
                hunks.append((new_start, new_count))
        elif line.startswith("-") and not line.startswith("---"):
            removed.append(line[1:])
    if old_path is not None or path is not None:
        yield path, old_path, hunks, removed


def main():
    base_ref = os.environ.get("VIBETAGS_BASE_REF", "").strip()
    warn_only = os.environ.get("VIBETAGS_WARN_ONLY", "false").strip().lower() == "true"
    if not base_ref:
        raise SystemExit("VIBETAGS_BASE_REF is not set -- pass the PR base ref/SHA")
    # Option-injection guard: refs/SHAs never start with '-'. Reject so an attacker-influenced
    # base ref cannot be reinterpreted as a git option by `git merge-base`.
    if base_ref.startswith("-"):
        raise SystemExit("VIBETAGS_BASE_REF must not start with '-'")

    repo_root = run_git("rev-parse", "--show-toplevel").strip()
    merge_base = run_git("merge-base", base_ref, "HEAD").strip()

    reports = find_reports(os.getcwd())
    if not reports:
        print("::warning::VibeTags locked-files guard: no .vibetags-locks report found -- "
              "did the build step run with the file touched?")
        return 0
    locks = load_locks(reports, repo_root)
    print(f"VibeTags locked-files guard: {len(locks)} locked element(s) "
          f"from {len(reports)} report(s); diffing against {merge_base[:12]}")

    # Trailing "--" terminates options/refs so no value can be reinterpreted as a pathspec/flag.
    diff_text = run_git("diff", "--unified=0", "--no-color", merge_base, "HEAD", "--")
    kind = "warning" if warn_only else "error"
    violations = 0

    for path, old_path, hunks, removed_lines in parse_diff(diff_text):
        display = path or old_path or "?"
        if display.endswith(".vibetags-locks"):
            continue  # report regeneration is expected, not a violation

        # 3. Deleted file that contained @AILocked at base.
        if path is None and old_path is not None:
            base_blob = subprocess.run(
                ["git", "show", f"{merge_base}:{old_path}"],
                capture_output=True, text=True, encoding="utf-8", errors="replace",
            )
            if base_blob.returncode == 0 and "@AILocked" in base_blob.stdout:
                violations += 1
                print(f"::{kind} file={old_path}::Deleted file contained @AILocked code")
            continue

        # 2. Lock stripping: the @AILocked annotation itself was removed.
        if any("@AILocked" in line for line in removed_lines):
            violations += 1
            print(f"::{kind} file={display}::A line containing @AILocked was removed -- "
                  "removing a lock requires explicit human review")

        # 1. Changed lines intersect a locked range at HEAD.
        for lock in locks_for(locks, display):
            start, end = lock.get("startLine"), lock.get("endLine")
            element = lock.get("element", "?")
            reason = lock.get("reason", "")
            if start is None or end is None:
                # No positions (non-javac compiler): file-level fallback.
                violations += 1
                print(f"::{kind} file={display}::Change touches a file containing locked "
                      f"element {element} (no line info available). Reason: {reason}")
                continue
            for new_start, new_count in hunks:
                if new_count == 0:
                    touched = start - 1 <= new_start <= end  # pure deletion at this position
                else:
                    touched = new_start <= end and (new_start + new_count - 1) >= start
                if touched:
                    violations += 1
                    print(f"::{kind} file={display},line={max(new_start, 1)}::Change touches "
                          f"@AILocked element {element} (lines {start}-{end}). Reason: {reason}")
                    break

    if violations:
        verdict = "warning(s)" if warn_only else "violation(s)"
        print(f"\nVibeTags locked-files guard: {violations} {verdict} found.")
        return 0 if warn_only else 1
    print("\nVibeTags locked-files guard: no locked code touched.")
    return 0


if __name__ == "__main__":
    sys.exit(main())
