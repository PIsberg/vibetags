#!/usr/bin/env python3
"""
Generates docs/vibetags-demo.cast from real VibeTags output.
Run from the repo root: python tools/make-demo-cast.py
"""
import json, sys

WIDTH, HEIGHT = 82, 26

G  = "\033[0;32m"   # green
DIM = "\033[2m"
B  = "\033[1m"
R  = "\033[0m"      # reset

events = []
t = 0.0

def pause(s):
    global t
    t += s

def out(text, pre=0.0):
    global t
    t += pre
    events.append([round(t, 3), "o", text])

def nl(text="", pre=0.05):
    out(text + "\r\n", pre)

def comment(text, pre=0.5):
    out(f"{DIM}# {text}{R}\r\n", pre)

def cmd(text, pre=0.4):
    out(f"{G}${R} {B}{text}{R}\r\n", pre)


# ── Header ─────────────────────────────────────────────────────────────────
out("\033[2J\033[H")          # clear screen
nl(f"{B}VibeTags{R} — annotate once, all AI platforms stay in sync")
nl()
pause(0.4)

# ── 1. Show the annotated Java file ────────────────────────────────────────
comment("1. A class annotated with three guardrails")
cmd("cat src/main/java/com/example/PaymentService.java")
pause(0.3)

java = """\
package com.example;

import se.deversity.vibetags.annotations.AIContract;
import se.deversity.vibetags.annotations.AILocked;
import se.deversity.vibetags.annotations.AIPrivacy;

@AILocked(reason = "PCI-DSS scope — do not modify without a security review")
public class PaymentService {

    @AIPrivacy(reason = "Card data — never log or expose in suggestions")
    private final String cardToken;

    public PaymentService(String cardToken) { this.cardToken = cardToken; }

    @AIContract(reason = "Bound by OpenAPI v2 contract with mobile-app")
    public String charge(double amount, String currency) {
        return "tx-" + System.currentTimeMillis();
    }
}"""
for line in java.splitlines():
    nl(line, pre=0.04)
nl()
pause(0.8)

# ── 2. Show placeholder files (empty) ──────────────────────────────────────
comment("2. Placeholder files — empty before compile")
cmd("wc -c CLAUDE.md .cursorrules AGENTS.md")
pause(0.3)
for name in ["CLAUDE.md", ".cursorrules", "AGENTS.md"]:
    nl(f"       0 {name}", pre=0.04)
nl("       0 total", pre=0.04)
nl()
pause(0.5)

# ── 3. Compile ──────────────────────────────────────────────────────────────
comment("3. mvn compile -q  — VibeTags runs as part of javac")
cmd("mvn compile -q")
pause(2.8)   # simulate compilation time
nl()
pause(0.3)

# ── 4. Show generated CLAUDE.md ─────────────────────────────────────────────
comment("4. CLAUDE.md — generated XML guardrails")
cmd("sed -n '/VIBETAGS-START/,/VIBETAGS-END/p' CLAUDE.md")
pause(0.3)

claude = """\
<!-- VIBETAGS-START -->
<!-- # Generated by VibeTags | https://github.com/PIsberg/vibetags -->
<project_guardrails>
  <locked_files>
    <file path="com.example.PaymentService">
      <reason>PCI-DSS scope — do not modify without a security review</reason>
    </file>
  </locked_files>
  <pii_guardrails>
    <element path="com.example.PaymentService.cardToken">
      <reason>Card data — never log or expose in suggestions</reason>
    </element>
  </pii_guardrails>
  <contract_signatures>
    <element path="com.example.PaymentService.charge(double,java.lang.String)">
      <reason>Bound by OpenAPI v2 contract with mobile-app</reason>
    </element>
  </contract_signatures>
</project_guardrails>
<rule>Never propose edits to files listed in &lt;locked_files&gt;.</rule>
<!-- VIBETAGS-END -->"""
for line in claude.splitlines():
    nl(line, pre=0.04)
nl()
pause(0.8)

# ── 5. Show generated .cursorrules ──────────────────────────────────────────
comment("5. .cursorrules — same guardrails in Cursor's format")
cmd("sed -n '/VIBETAGS-START/,/VIBETAGS-END/p' .cursorrules")
pause(0.3)

cursor = """\
# VIBETAGS-START
## LOCKED FILES (DO NOT EDIT)
* `com.example.PaymentService`
  PCI-DSS scope — do not modify without a security review

## \U0001f512 PII / PRIVACY GUARDRAILS
* `com.example.PaymentService.cardToken`
  Card data — never log or expose in suggestions

## \U0001f510 CONTRACT-FROZEN SIGNATURES
* `com.example.PaymentService.charge(double,java.lang.String)`
  Bound by OpenAPI v2 contract with mobile-app and checkout-service
# VIBETAGS-END"""
for line in cursor.splitlines():
    nl(line, pre=0.04)
nl()
pause(0.5)

# ── Done ────────────────────────────────────────────────────────────────────
out(f"{G}✓ 3 platform files updated — from a single mvn compile{R}\r\n")
pause(2.5)

# ── Write ────────────────────────────────────────────────────────────────────
out_path = sys.argv[1] if len(sys.argv) > 1 else "docs/vibetags-demo.cast"
header = {"version": 2, "width": WIDTH, "height": HEIGHT,
          "title": "VibeTags demo"}
with open(out_path, "w", encoding="utf-8") as f:
    f.write(json.dumps(header) + "\n")
    for ev in events:
        f.write(json.dumps(ev, ensure_ascii=False) + "\n")

print(f"wrote {len(events)} events to {out_path}")
