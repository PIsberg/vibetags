    /**
     * Resolves which primary services should have their files written.
     * Only "signal" files (like QWEN.md or .cursorrules) are checked — their presence acts as the opt-in.
     *
     * @param messager        the compiler messager for printing notes
     * @param allServiceFiles the map of all possible service files
     * @return the set of active service keys based on existing files
     */
    public static Set<String> resolveActiveServices(Messager messager, Map<String, Path> allServiceFiles) {
        // Only these primary files define an "active" service
        Set<String> optInKeys = Set.of(
            "cursor", "claude", "aiexclude", "codex", "gemini", "copilot", "qwen",
            "cursor_ignore", "claude_ignore", "copilot_ignore", "qwen_ignore",
            "llms", "llms_full"
        );

        Set<String> active = allServiceFiles.entrySet().stream()
            .filter(e -> optInKeys.contains(e.getKey()))
            .filter(e -> Files.exists(e.getValue()))
            .map(Map.Entry::getKey)
            .collect(java.util.stream.Collectors.toSet());

        if (active.isEmpty()) {
            StringBuilder msg = new StringBuilder(
                "VibeTags: No AI config files found - nothing will be generated.\n" +
                "Create one or more of the following files in your project root to opt in:\n");
            allServiceFiles.entrySet().stream()
                .filter(e -> optInKeys.contains(e.getKey()))
                .forEach(e -> msg.append("  ").append(e.getValue().getFileName()).append("\n"));
            messager.printMessage(javax.tools.Diagnostic.Kind.NOTE, msg.toString());
        }

        return active;
    }

    /**
     * Determines the appropriate VibeTags markers for a given file.
     *
     * @param fileName the name of the file
     * @return an array containing [startMarker, endMarker], or {@code null} if markers are not supported
     */
    private String[] getMarkersFor(String fileName) {
        if (fileName.endsWith(".md") || fileName.equals("llms.txt") || fileName.equals("llms-full.txt")) {
            return new String[]{MARKER_START_MD, MARKER_END_MD};
        }
        if (fileName.endsWith(".json") || fileName.endsWith(".toml")) {
            return null; // No markers for JSON/TOML, overwrite only
        }
        // Default to hash comments for rules/ignore files (.cursorrules, .aiexclude, etc.)
        return new String[]{MARKER_START_HASH, MARKER_END_HASH};
    }

    /**
     * Writes {@code content} to {@code path} only if the file's current content differs.
     *
     * @param path    the destination file path
     * @param content the content to write
     * @return {@code true} if the file was written (content changed or file was empty),
     *         {@code false} if the existing content was already up-to-date.
     */
    public boolean writeFileIfChanged(String path, String content) {
        try {
            Path filePath = Paths.get(path);
            String fileName = filePath.getFileName().toString();
            String[] markers = getMarkersFor(fileName);

            // Wrap new content in markers if supported
            String wrappedContent = (markers != null)
                ? markers[0] + "\n" + content.trim() + "\n" + markers[1]
                : content;

            if (Files.exists(filePath)) {
                String existing = Files.readString(filePath, java.nio.charset.StandardCharsets.UTF_8);

                if (markers != null) {
                    int startIdx = existing.indexOf(markers[0]);
                    int endIdx = existing.indexOf(markers[1]);

                    if (startIdx != -1 && endIdx != -1 && startIdx < endIdx) {
                        // Section exists: replace it
                        String before = existing.substring(0, startIdx).stripTrailing();
                        String after = existing.substring(endIdx + markers[1].length()).stripLeading();

                        StringBuilder sb = new StringBuilder();
                        if (!before.isEmpty()) sb.append(before).append("\n\n");
                        sb.append(wrappedContent);
                        if (!after.isEmpty()) sb.append("\n\n").append(after);
                        String updated = sb.toString();

                        if (existing.strip().equals(updated.strip())) {
                            return false;
                        }
                        Files.writeString(filePath, updated, java.nio.charset.StandardCharsets.UTF_8);
                        return true;
                    } else if (existing.contains(GENERATED_HEADER.trim())) {
                        // Legacy VibeTags file found (no markers but has header): upgrade to markers
                        if (existing.strip().equals(wrappedContent.trim())) {
                            return false;
                        }
                        Files.writeString(filePath, wrappedContent, java.nio.charset.StandardCharsets.UTF_8);
                        return true;
                    } else {
                        // Section missing and not a legacy VibeTags file: append to the end
                        String updated = existing.stripTrailing() + "\n\n" + wrappedContent + "\n";
                        Files.writeString(filePath, updated, java.nio.charset.StandardCharsets.UTF_8);
                        return true;
                    }
                } else {
                    // Markers not supported (JSON/TOML): standard overwrite-if-changed logic
                    if (existing.strip().equals(content.strip())) {
                        return false;
                    }
                }
            }

            // File doesn't exist or doesn't support markers: create with wrapped content
            Path parent = filePath.getParent();
            if (parent != null && !Files.exists(parent)) {
                Files.createDirectories(parent);
            }
            try (PrintWriter out = new PrintWriter(new FileWriter(path, java.nio.charset.StandardCharsets.UTF_8))) {
                out.println(wrappedContent.trim());
            }
            return true;
        } catch (IOException e) {
            processingEnv.getMessager().printMessage(
                javax.tools.Diagnostic.Kind.WARNING, "VibeTags: Failed to write AI rules file: " + path
            );
            return false;
        }
    }

