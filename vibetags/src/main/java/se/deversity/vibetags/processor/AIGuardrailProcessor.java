    @Override
    public boolean process(Set<? extends TypeElement> annotations, RoundEnvironment roundEnv) {
        if (processed || roundEnv.processingOver()) {
            if (roundEnv.processingOver() && !processed) {
                generateFiles();
                processed = true;
            }
            return false;
        }

        collector.collect(roundEnv);

        // Validation in each round for early feedback
        validateAnnotations(processingEnv.getMessager(), roundEnv);

        return false; // allow others to see the annotations
    }

    private void generateFiles() {
        if (log != null) {
            log.info("VibeTags v{} | {}", VERSION, GITHUB_URL);
            log.info("Root: {}", root.toAbsolutePath());
        }

        // Collect element names for logging
        List<String> lockedNames  = lockedElements.stream().map(e -> e.getSimpleName().toString()).collect(Collectors.toList());
        List<String> contextNames = contextElements.stream().map(e -> e.getSimpleName().toString()).collect(Collectors.toList());
        List<String> ignoreNames  = ignoreElements.stream().map(e -> e.getSimpleName().toString()).collect(Collectors.toList());
        List<String> auditNames   = auditElements.stream().map(e -> e.getSimpleName().toString()).collect(Collectors.toList());
        List<String> draftNames   = draftElements.stream().map(e -> e.getSimpleName().toString()).collect(Collectors.toList());
        List<String> privacyNames = privacyElements.stream().map(e -> e.getSimpleName().toString()).collect(Collectors.toList());
        List<String> coreNames    = coreElements.stream().map(e -> e.getSimpleName().toString()).collect(Collectors.toList());
        List<String> performanceNames = performanceElements.stream().map(e -> e.getSimpleName().toString()).collect(Collectors.toList());

        // Signal directories for granular rules
        Map<String, Path> serviceFiles = buildServiceFileMap(root);
        Set<String> activeServices = resolveActiveServices(processingEnv.getMessager(), serviceFiles);

        // Cache active-service flags so per-element loops can skip building strings
        // for platforms that won't have a file written. Per-element appends dominate
        // generateFiles cost at high N, so this is the main load-test lever.
        boolean cursorActive   = activeServices.contains("cursor");
        boolean claudeActive   = activeServices.contains("claude");
        boolean aiexcludeActive = activeServices.contains("aiexclude");
        boolean codexActive    = activeServices.contains("codex");
        boolean copilotActive  = activeServices.contains("copilot");
        boolean qwenActive     = activeServices.contains("qwen");
        boolean geminiActive   = activeServices.contains("gemini");
        boolean llmsActive     = activeServices.contains("llms");
        boolean llmsFullActive = activeServices.contains("llms_full");
        boolean aiderConvActive   = activeServices.contains("aider_conventions");
        boolean aiderIgnoreActive = activeServices.contains("aider_ignore");
        boolean cursorIgnoreFileActive  = activeServices.contains("cursor_ignore");
        boolean claudeIgnoreFileActive  = activeServices.contains("claude_ignore");
        boolean copilotIgnoreFileActive = activeServices.contains("copilot_ignore");
        boolean qwenIgnoreFileActive    = activeServices.contains("qwen_ignore");
        boolean granularActive = activeServices.contains("cursor_granular")
            || activeServices.contains("trae_granular") || activeServices.contains("roo_granular");

        // Per-platform section builders for Gemini (assembled in the finalize section below)
        StringBuilder geminiLocked = new StringBuilder();
        StringBuilder geminiContext = new StringBuilder();

        // Process @AILocked
        for (Element element : lockedElements) {
            AILocked locked = element.getAnnotation(AILocked.class);
            String className = elementPath(element);
            String reason = locked.reason();

            if (cursorActive) cursorRules.append("* `").append(className).append("` - Reason: ").append(reason).append("\n");
            if (claudeActive) claudeMd.append("    <file path=\"").append(className).append("\">\n")
                    .append("      <reason>").append(reason).append("</reason>\n")
                    .append("    </file>\n");
            if (aiexcludeActive) aiExclude.append("**/").append(element.getSimpleName()).append(".java\n");
            if (codexActive) codexAgents.append("- **").append(className).append("**: ").append(reason).append("\n");
            if (copilotActive) copilot.append("- `").append(className).append("` - ").append(reason).append("\n");
            if (qwenActive) qwenMd.append("* `").append(className).append("` - ").append(reason).append("\n");
            if (geminiActive) geminiLocked.append("- `").append(className).append("`: ").append(reason).append("\n");

            if (llmsActive) llmsTxt.append("- [").append(elementDisplayName(element)).append("](").append(className).append("): ").append(reason).append("\n");
            if (llmsFullActive) llmsFullTxt.append("### ").append(className).append("\n")
                       .append("- **Reason**: ").append(reason).append("\n\n");

            if (aiderConvActive) aiderConventions.append("#### LOCKED: ").append(className).append("\n")
                            .append("- **Status**: Locked (Do Not Edit)\n")
                            .append("- **Reason**: ").append(reason).append("\n\n");
            if (granularActive) appendToGranular(element, "Locked Status", "- **Reason**: " + reason);
        }

        claudeMd.append("  </locked_files>\n  <contextual_instructions>\n");
        cursorRules.append("\n## CONTEXTUAL RULES\n");
        codexAgents.append("\n## CONTEXTUAL RULES\n");
        copilot.append("\n## Contextual Guidelines\n");
        qwenMd.append("\n## CONTEXTUAL RULES\n");

        // Process @AIContext
        for (Element element : contextElements) {
            AIContext context = element.getAnnotation(AIContext.class);
            String className = elementPath(element);

            if (cursorActive) cursorRules.append("* `").append(className).append("`\n  * Focus: ").append(context.focus())
                       .append("\n  * Avoid: ").append(context.avoids()).append("\n");
            if (claudeActive) claudeMd.append("    <file path=\"").append(className).append("\">\n")
                    .append("      <focus>").append(context.focus()).append("</focus>\n")
                    .append("      <avoids>").append(context.avoids()).append("</avoids>\n")
                    .append("    </file>\n");
            if (codexActive) codexAgents.append("- `").append(className).append("`: Focus on ").append(context.focus())
                       .append(". Avoid ").append(context.avoids()).append(".\n");
            if (copilotActive) copilot.append("- `").append(className).append("` \n")
                   .append("  - Focus: ").append(context.focus()).append("\n")
                   .append("  - Avoid: ").append(context.avoids()).append("\n");
            if (qwenActive) qwenMd.append("* `").append(className).append("` \n")
                  .append("  * Focus: ").append(context.focus()).append("\n")
                  .append("  * Avoid: ").append(context.avoids()).append("\n");

            if (llmsActive) llmsTxtContext.append("- [").append(elementDisplayName(element)).append("](").append(className)
                          .append("): Focus - ").append(context.focus())
                          .append(". Avoid - ").append(context.avoids()).append("\n");
            if (llmsFullActive) llmsFullTxtContext.append("### ").append(className).append("\n")
                              .append("- **Focus**: ").append(context.focus()).append("\n")
                              .append("- **Avoid**: ").append(context.avoids()).append("\n\n");

            if (aiderConvActive) aiderConventions.append("#### CONTEXT: ").append(className).append("\n")
                            .append("- **Focus**: ").append(context.focus()).append("\n")
                            .append("- **Avoid**: ").append(context.avoids()).append("\n\n");
            if (granularActive) appendToGranular(element, "Context & Focus", "- **Focus**: " + context.focus() + "\n- **Avoid**: " + context.avoids());
            if (geminiActive) geminiContext.append("- `").append(className).append("`: Focus - ").append(context.focus())
                         .append(". Avoid - ").append(context.avoids()).append("\n");
        }
        claudeMd.append("  </contextual_instructions>\n");

        // Builders for ignored elements
        StringBuilder claudeIgnore = new StringBuilder("  <ignored_elements>\n");
        StringBuilder codexIgnore = new StringBuilder("\n## IGNORED ELEMENTS\nThe following elements must be completely excluded from AI context and completions:\n\n");
        StringBuilder geminiIgnore = new StringBuilder("## IGNORED ELEMENTS\nThe following elements must be completely excluded from AI context and completions:\n\n");
        StringBuilder copilotIgnore = new StringBuilder("\n## Ignored Elements\nDo not reference or suggest changes to the following:\n\n");
        StringBuilder qwenIgnore = new StringBuilder("\n## IGNORED ELEMENTS\nThe following elements must be completely excluded from AI's memory and context:\n\n");

        // Process @AIIgnore
        for (Element element : ignoreElements) {
            String className = elementPath(element);
            if (cursorActive) cursorIgnore.append("* `").append(className).append("` \n");
            if (claudeActive) claudeIgnore.append("    <file path=\"").append(className).append("\"/>\n");
            if (aiexcludeActive) aiExclude.append("**/").append(element.getSimpleName()).append(".java\n");
            if (codexActive) codexIgnore.append("- `").append(className).append("` \n");
            if (geminiActive) geminiIgnore.append("- `").append(className).append("` \n");
            if (copilotActive) copilotIgnore.append("- `").append(className).append("` \n");

            String globPattern = "**/"+ element.getSimpleName() + ".java\n";
            if (cursorIgnoreFileActive)  cursorIgnoreFile.append(globPattern);
            if (claudeIgnoreFileActive)  claudeIgnoreFile.append(globPattern);
            if (copilotIgnoreFileActive) copilotIgnoreFile.append(globPattern);
            if (qwenIgnoreFileActive)    qwenIgnoreFile.append(globPattern);
            if (qwenActive) qwenIgnore.append("* `").append(className).append("` \n");

            if (llmsActive) llmsTxtIgnore.append("- [").append(elementDisplayName(element)).append("](").append(className)
                         .append("): excluded from AI context\n");
            if (llmsFullActive) llmsFullTxtIgnore.append("### ").append(className).append("\n")
                             .append("- Excluded from AI context entirely - treat as non-existent\n\n");

            if (aiderIgnoreActive) aiderIgnore.append(globPattern);
            if (aiderConvActive) aiderConventions.append("#### IGNORE: ").append(className).append("\n")
                            .append("- **Instruction**: This element is strictly excluded from AI context. Do not reference it.\n\n");
            if (granularActive) appendToGranular(element, "Exclusion Rule", "This element is strictly excluded from AI context. Do not reference it.");
        }

        // Builders for audit sections
        StringBuilder cursorAudit = new StringBuilder("\n## 🛡️ MANDATORY SECURITY AUDITS\nWhen proposing edits or writing code for the following files, you MUST perform a security review before outputting the final code. You must explicitly state in your response that you have audited the changes for the required vulnerabilities.\n\n");
        StringBuilder claudeAudit = new StringBuilder("\n  <audit_requirements>\n");
        // geminiMd assembled lazily from geminiLocked/geminiContext/geminiAudit in the finalize section
        StringBuilder geminiMd = new StringBuilder("# GEMINI AI INSTRUCTIONS\n" + GENERATED_HEADER + "\n");
        StringBuilder geminiAudit = new StringBuilder();
        StringBuilder codexAudit = new StringBuilder("\n## 🛡️ MANDATORY SECURITY AUDITS\nWhen proposing edits or writing code for the following files, you MUST perform a security review before outputting the final code. You must explicitly state in your response that you have audited the changes for the required vulnerabilities.\n\n");
        StringBuilder copilotAudit = new StringBuilder("\n## Security Audit Requirements\nBefore suggesting changes to the following files, audit for the listed vulnerabilities:\n\n");
        StringBuilder qwenAudit = new StringBuilder("\n## 🛡️ MANDATORY SECURITY AUDITS\nWhen proposing edits or writing code for the following files, you MUST perform a security review. Explicitly state that you have audited the changes for the listed vulnerabilities.\n\n");

        // Process @AIAudit
        for (Element element : auditElements) {
            AIAudit audit = element.getAnnotation(AIAudit.class);
            if (audit == null) continue;
            String className = elementPath(element);
            String[] checkFor = audit.checkFor();
            if (checkFor.length == 0) continue;
            String checkForJoined = String.join(", ", checkFor);

            if (cursorActive) {
                cursorAudit.append("* `").append(className).append("` \n");
                cursorAudit.append("  - Required Checks: ").append(checkForJoined).append("\n");
            }
            if (claudeActive) {
                claudeAudit.append("    <file path=\"").append(className).append("\">\n");
                for (String vulnerability : checkFor) {
                    claudeAudit.append("      <vulnerability_check>").append(vulnerability).append("</vulnerability_check>\n");
                }
                claudeAudit.append("    </file>\n");
            }
            if (geminiActive) {
                geminiAudit.append("File: `").append(className).append("` ")
                           .append("\nCritical Vulnerabilities to Prevent: ");
                for (String vulnerability : checkFor) {
                    geminiAudit.append("\n- ").append(vulnerability);
                }
                geminiAudit.append("\n\n");
            }
            if (codexActive) codexAudit.append("* `").append(className).append("` \n")
                      .append("  - Required Checks: ").append(checkForJoined).append("\n");
            if (copilotActive) copilotAudit.append("- `").append(className).append("` \n")
                        .append("  - Required Checks: ").append(checkForJoined).append("\n");
            if (qwenActive) qwenAudit.append("* `").append(className).append("` \n")
                     .append("  - Required Checks: ").append(checkForJoined).append("\n");

            if (llmsActive) llmsTxtAudit.append("- [").append(elementDisplayName(element)).append("](").append(className)
                        .append("): check for ").append(checkForJoined).append("\n");
            if (llmsFullActive) llmsFullTxtAudit.append("### ").append(className).append("\n")
                            .append("- **Required Checks**: ").append(checkForJoined).append("\n\n");

            if (aiderConvActive) aiderConventions.append("#### SECURITY AUDIT: ").append(className).append("\n")
                            .append("- **Required Checks**: ").append(checkForJoined).append("\n\n");
            if (granularActive) appendToGranular(element, "Security Audit Requirements", "When modifying this element, audit for:\n- " + String.join("\n- ", checkFor));
        }

        // Builders for draft elements
        StringBuilder cursorDraft = new StringBuilder("\n## 📝 IMPLEMENTATION TASKS (TODO)\n" +
            "The following elements are currently in DRAFT mode. Follow the instructions to implement them:\n\n");
        StringBuilder claudeDraft = new StringBuilder("  <implementation_tasks>\n");
        StringBuilder codexDraft = new StringBuilder("\n## IMPLEMENTATION TASKS\nThe following elements are drafts that need implementation:\n\n");
        StringBuilder geminiDraft = new StringBuilder("## IMPLEMENTATION TASKS\nThe following elements are drafts that need implementation:\n\n");
        StringBuilder copilotDraft = new StringBuilder("\n## Implementation Tasks\nFollow these instructions to implement the drafts:\n\n");
        StringBuilder qwenDraft = new StringBuilder("\n## IMPLEMENTATION TASKS\nThe following elements are drafts that need implementation:\n\n");

        // Process @AIDraft
        for (Element element : draftElements) {
            AIDraft draft = element.getAnnotation(AIDraft.class);
            if (draft == null) continue;
            String className = elementPath(element);
            String instructions = draft.instructions();

            if (cursorActive) cursorDraft.append("* `").append(className).append("` - Task: ").append(instructions).append("\n");
            if (claudeActive) claudeDraft.append("    <task path=\"").append(className).append("\">\n")
                       .append("      <instructions>").append(instructions).append("</instructions>\n")
                       .append("    </task>\n");
            if (codexActive) codexDraft.append("- **").append(className).append("**: ").append(instructions).append("\n");
            if (geminiActive) geminiDraft.append("- `").append(className).append("`: ").append(instructions).append("\n");
            if (copilotActive) copilotDraft.append("- `").append(className).append("`: ").append(instructions).append("\n");
            if (qwenActive) qwenDraft.append("* `").append(className).append("` - Task: ").append(instructions).append("\n");

            if (llmsActive) llmsTxtDraft.append("- [").append(elementDisplayName(element)).append("](").append(className)
                        .append("): ").append(instructions).append("\n");
            if (llmsFullActive) llmsFullTxtDraft.append("### ").append(className).append("\n")
                            .append("- **Instructions**: ").append(instructions).append("\n\n");

            if (aiderConvActive) aiderConventions.append("#### DRAFT/TODO: ").append(className).append("\n")
                            .append("- **Instruction**: ").append(instructions).append("\n\n");
            if (granularActive) appendToGranular(element, "Implementation Tasks", "- **Instruction**: " + instructions);
        }

        // Builders for privacy elements
        StringBuilder cursorPrivacy = new StringBuilder("\n## \uD83D\uDD12 PII / PRIVACY GUARDRAILS\n" +
            "The following elements handle Personally Identifiable Information (PII).\n" +
            "NEVER include their runtime values in logs, console output, external API calls,\n" +
            "test fixtures, mock data, or code suggestions.\n\n");
        StringBuilder claudePrivacy = new StringBuilder("  <pii_guardrails>\n");
        StringBuilder codexPrivacy = new StringBuilder("\n## \uD83D\uDD12 PII / PRIVACY GUARDRAILS\n" +
            "The following elements handle PII. Never include their runtime values in logs,\n" +
            "console output, external API calls, test fixtures, or mock data.\n\n");
        StringBuilder geminiPrivacy = new StringBuilder("## PII / PRIVACY GUARDRAILS\n" +
            "The following elements handle Personally Identifiable Information (PII).\n" +
            "Never include their runtime values in logs, console output, external API calls,\n" +
            "test fixtures, mock data, or code suggestions.\n\n");
        StringBuilder copilotPrivacy = new StringBuilder("\n## PII / Privacy Guardrails\n" +
            "Never log, expose, or suggest code that outputs the runtime values of these elements:\n\n");
        StringBuilder qwenPrivacy = new StringBuilder("\n## \uD83D\uDD12 PII / PRIVACY GUARDRAILS\n" +
            "The following elements handle PII. Never include their runtime values in logs,\n" +
            "console output, external API calls, test fixtures, or mock data.\n\n");

        // Process @AIPrivacy
        for (Element element : privacyElements) {
            AIPrivacy privacy = element.getAnnotation(AIPrivacy.class);
            if (privacy == null) continue;
            String className = elementPath(element);
            String reason = privacy.reason();

            if (cursorActive) cursorPrivacy.append("* `").append(className).append("` - ").append(reason).append("\n");
            if (claudeActive) claudePrivacy.append("    <element path=\"").append(className).append("\">\n")
                         .append("      <reason>").append(reason).append("</reason>\n")
                         .append("    </element>\n");
            if (codexActive) codexPrivacy.append("- `").append(className).append("`: ").append(reason).append("\n");
            if (geminiActive) geminiPrivacy.append("- `").append(className).append("`: ").append(reason).append("\n");
            if (copilotActive) copilotPrivacy.append("- `").append(className).append("` - ").append(reason).append("\n");
            if (qwenActive) qwenPrivacy.append("* `").append(className).append("` - ").append(reason).append("\n");

            if (llmsActive) llmsTxtPrivacy.append("- [").append(elementDisplayName(element)).append("](").append(className)
                          .append("): ").append(reason).append("\n");
            if (llmsFullActive) llmsFullTxtPrivacy.append("### ").append(className).append("\n")
                              .append("- **Reason**: ").append(reason).append("\n\n");

            if (aiderConvActive) aiderConventions.append("#### PRIVACY/PII: ").append(className).append("\n")
                            .append("- **Safety Rule**: Never log or expose runtime values of this element.\n")
                            .append("- **Reason**: ").append(reason).append("\n\n");
            if (granularActive) appendToGranular(element, "PII / Privacy Guardrails", "- **Rule**: Never log or expose runtime values of this element.\n- **Reason**: " + reason);
        }

        // Platform-specific builders for @AICore
        StringBuilder cursorCore = new StringBuilder("\n## \uD83E\uDDE0 CORE FUNCTIONALITY (CHANGE WITH EXTREME CAUTION)\n" +
            "The following elements are well-tested core components. Make changes with extreme caution.\n\n");
        StringBuilder claudeCore = new StringBuilder("  <core_elements>\n");
        StringBuilder codexCore  = new StringBuilder("\n## \uD83E\uDDE0 CORE FUNCTIONALITY\nThe following elements are well-tested core components. Make changes with extreme caution.\n\n");
        StringBuilder copilotCore = new StringBuilder("\n## Core Functionality (Extreme Caution)\nThe following elements are well-tested core components — change with extreme caution:\n\n");
        StringBuilder qwenCore   = new StringBuilder("\n## \uD83E\uDDE0 CORE FUNCTIONALITY\nThe following elements are well-tested core components. Make changes with extreme caution.\n\n");
        StringBuilder geminiCore = new StringBuilder();

        // Platform-specific builders for @AIPerformance
        StringBuilder cursorPerf = new StringBuilder("\n## \u26A1 PERFORMANCE CONSTRAINTS (HOT PATH)\n" +
            "The following elements are on a hot path. Never introduce O(n\u00B2) complexity. Always reason about time/space before proposing changes.\n\n");
        StringBuilder claudePerf = new StringBuilder("  <performance_constraints>\n");
        StringBuilder codexPerf  = new StringBuilder("\n## \u26A1 PERFORMANCE CONSTRAINTS\nHot-path elements — never introduce O(n\u00B2) or worse. Always reason about complexity before proposing changes.\n\n");
        StringBuilder copilotPerf = new StringBuilder("\n## Performance Constraints\nThe following elements are on a hot path — always reason about time and space complexity:\n\n");
        StringBuilder qwenPerf   = new StringBuilder("\n## \u26A1 PERFORMANCE CONSTRAINTS\nHot-path elements — O(n\u00B2) complexity is forbidden. Reason about complexity before proposing changes.\n\n");
        StringBuilder geminiPerf = new StringBuilder();

        // Process @AICore
        for (Element element : coreElements) {
            AICore core = element.getAnnotation(AICore.class);
            if (core == null) continue;
            String className = elementPath(element);
            String sensitivity = core.sensitivity();
            String note = core.note();

            if (cursorActive) cursorCore.append("* `").append(className).append("` - Sensitivity: ").append(sensitivity)
                      .append(". Note: ").append(note).append("\n");
            if (claudeActive) claudeCore.append("    <element path=\"").append(className).append("\">\n")
                      .append("      <sensitivity>").append(sensitivity).append("</sensitivity>\n")
                      .append("      <note>").append(note).append("</note>\n")
                      .append("    </element>\n");
            if (codexActive) codexCore.append("- **").append(className).append("** (sensitivity: ").append(sensitivity)
                     .append("): ").append(note).append("\n");
            if (copilotActive) copilotCore.append("- `").append(className).append("` — sensitivity: ").append(sensitivity)
                       .append(". ").append(note).append("\n");
            if (qwenActive) qwenCore.append("* `").append(className).append("` - Sensitivity: ").append(sensitivity)
                    .append(". Note: ").append(note).append("\n");
            if (geminiActive) geminiCore.append("- `").append(className).append("`: Sensitivity: ").append(sensitivity)
                      .append(". Note: ").append(note).append("\n");

            if (llmsActive) llmsTxtCore.append("- [").append(elementDisplayName(element)).append("](").append(className)
                       .append("): Sensitivity: ").append(sensitivity).append(". Note: ").append(note).append("\n");
            if (llmsFullActive) llmsFullTxtCore.append("### ").append(className).append("\n")
                           .append("- **Sensitivity**: ").append(sensitivity).append("\n")
                           .append("- **Note**: ").append(note).append("\n\n");

            if (aiderConvActive) aiderConventions.append("#### CORE FUNCTIONALITY: ").append(className).append("\n")
                            .append("- **Sensitivity**: ").append(sensitivity).append("\n")
                            .append("- **Note**: ").append(note).append("\n\n");
            if (granularActive) appendToGranular(element, "Core Functionality", "- **Sensitivity**: " + sensitivity + "\n- **Note**: " + note);
        }

        // Process @AIPerformance
        for (Element element : performanceElements) {
            AIPerformance perf = element.getAnnotation(AIPerformance.class);
            if (perf == null) continue;
            String className = elementPath(element);
            String constraint = perf.constraint();

            if (cursorActive) cursorPerf.append("* `").append(className).append("` - ").append(constraint).append("\n");
            if (claudeActive) claudePerf.append("    <element path=\"").append(className).append("\">\n")
                      .append("      <constraint>").append(constraint).append("</constraint>\n")
                      .append("    </element>\n");
            if (codexActive) codexPerf.append("- **").append(className).append("**: ").append(constraint).append("\n");
            if (copilotActive) copilotPerf.append("- `").append(className).append("`: ").append(constraint).append("\n");
            if (qwenActive) qwenPerf.append("* `").append(className).append("` - ").append(constraint).append("\n");
            if (geminiActive) geminiPerf.append("- `").append(className).append("`: ").append(constraint).append("\n");

            if (llmsActive) llmsTxtPerformance.append("- [").append(elementDisplayName(element)).append("](").append(className)
                              .append("): ").append(constraint).append("\n");
            if (llmsFullActive) llmsFullTxtPerformance.append("### ").append(className).append("\n")
                                  .append("- **Constraint**: ").append(constraint).append("\n\n");

            if (aiderConvActive) aiderConventions.append("#### PERFORMANCE CONSTRAINTS: ").append(className).append("\n")
                            .append("- **Rule**: Optimal complexity required. O(n^2) is forbidden on hot paths.\n")
                            .append("- **Constraint**: ").append(constraint).append("\n\n");
            if (granularActive) appendToGranular(element, "Performance Constraints", "- **Rule**: Optimal complexity required. O(n^2) is forbidden on hot paths.\n- **Constraint**: " + constraint);
        }

        // Assemble Gemini content from per-annotation section builders
        if (!lockedElements.isEmpty()) {
            geminiMd.append("\n## LOCKED FILES (DO NOT MODIFY)\n")
                    .append("Do not suggest modifications to the following files:\n\n")
                    .append(geminiLocked);
        }
        if (!contextElements.isEmpty()) {
            geminiMd.append("\n## CONTEXTUAL RULES\n")
                    .append("Apply the following context when assisting with these files:\n\n")
                    .append(geminiContext);
        }
        if (!auditElements.isEmpty()) {
            geminiMd.append("\n## CONTINUOUS AUDIT REQUIREMENTS\n")
                    .append("You are acting as a Senior Staff Engineer. Whenever you write code for the files listed below, you must ensure your completions and chat responses strictly prevent the listed vulnerabilities:\n\n")
                    .append(geminiAudit);
        }

        // Finalize sections
        if (!auditElements.isEmpty()) {
            cursorRules.append(cursorAudit);
            claudeAudit.append("  </audit_requirements>\n");
            claudeMd.append(claudeAudit);
            claudeMd.append("\n<rule>\n  If you are asked to modify any file listed in <audit_requirements>, you must first silently analyze your proposed code for the listed <vulnerability_check> items. If your code introduces these vulnerabilities, you must rewrite it before displaying it to the user.\n</rule>\n");
            codexAgents.append(codexAudit);
            copilot.append(copilotAudit);
            qwenMd.append(qwenAudit);
        }

        if (!ignoreElements.isEmpty()) {
            cursorRules.append(cursorIgnore);
            claudeIgnore.append("  </ignored_elements>\n");
            claudeMd.append(claudeIgnore);
            claudeMd.append("\n<rule>Never reference or suggest changes to any element listed in <ignored_elements>. Treat these as if they do not exist.</rule>\n");
            codexAgents.append(codexIgnore);
            geminiMd.append(geminiIgnore);
            copilot.append(copilotIgnore);
            qwenMd.append(qwenIgnore);
        }

        if (!draftElements.isEmpty()) {
            cursorRules.append(cursorDraft);
            claudeDraft.append("  </implementation_tasks>\n");
            claudeMd.append(claudeDraft);
            codexAgents.append(codexDraft);
            geminiMd.append(geminiDraft);
            copilot.append(copilotDraft);
            qwenMd.append(qwenDraft);
        }

        if (!privacyElements.isEmpty()) {
            cursorRules.append(cursorPrivacy);
            claudePrivacy.append("  </pii_guardrails>\n");
            claudeMd.append(claudePrivacy);
            claudeMd.append("\n<rule>\n  Never include runtime values of elements listed in <pii_guardrails> in logs, console output, external API calls, test fixtures, mock data, or code suggestions. Treat their values as strictly confidential.\n</rule>\n");
            codexAgents.append(codexPrivacy);
            geminiMd.append(geminiPrivacy);
            copilot.append(copilotPrivacy);
            qwenMd.append(qwenPrivacy);
        }

        if (!coreElements.isEmpty()) {
            cursorRules.append(cursorCore);
            claudeCore.append("  </core_elements>\n");
            claudeMd.append(claudeCore);
            claudeMd.append("\n<rule>Elements listed in <core_elements> are well-tested core components. Make changes with extreme caution and verify comprehensive test coverage before proposing modifications.</rule>\n");
            codexAgents.append(codexCore);
            copilot.append(copilotCore);
            qwenMd.append(qwenCore);
            geminiMd.append("\n## CORE FUNCTIONALITY (EXTREME CAUTION)\n")
                    .append("The following elements are well-tested core components. Make changes with extreme caution:\n\n")
                    .append(geminiCore);
        }

        if (!performanceElements.isEmpty()) {
            cursorRules.append(cursorPerf);
            claudePerf.append("  </performance_constraints>\n");
            claudeMd.append(claudePerf);
            claudeMd.append("\n<rule>Elements listed in <performance_constraints> are on a hot path. Never introduce O(n\u00B2) or worse complexity. Always reason about time and space complexity before suggesting changes.</rule>\n");
            codexAgents.append(codexPerf);
            copilot.append(copilotPerf);
            qwenMd.append(qwenPerf);
            geminiMd.append("\n## PERFORMANCE CONSTRAINTS (HOT PATH)\n")
                    .append("Never introduce O(n\u00B2) complexity into these elements. Always reason about complexity before proposing changes:\n\n")
                    .append(geminiPerf);
        }

        claudeMd.append("</project_guardrails>\n");
        claudeMd.append("\n<rule>Never propose edits to files listed in <locked_files>.</rule>\n");

        // Assemble llms.txt and llms-full.txt
        llmsTxt.append(llmsTxtContext);
        llmsFullTxt.append(llmsFullTxtContext);
        if (!auditElements.isEmpty()) { llmsTxt.append(llmsTxtAudit); llmsFullTxt.append(llmsFullTxtAudit); }
        if (!ignoreElements.isEmpty()) { llmsTxt.append(llmsTxtIgnore); llmsFullTxt.append(llmsFullTxtIgnore); }
        if (!draftElements.isEmpty()) { llmsTxt.append(llmsTxtDraft); llmsFullTxt.append(llmsFullTxtDraft); }
        if (!privacyElements.isEmpty()) { llmsTxt.append(llmsTxtPrivacy); llmsFullTxt.append(llmsFullTxtPrivacy); }
        if (!coreElements.isEmpty()) { llmsTxt.append(llmsTxtCore); llmsFullTxt.append(llmsFullTxtCore); }
        if (!performanceElements.isEmpty()) { llmsTxt.append(llmsTxtPerformance); llmsFullTxt.append(llmsFullTxtPerformance); }

        if (log != null) {
            log.info("Active services ({}): {}", activeServices.size(),
                activeServices.stream().sorted().collect(Collectors.joining(", ")));
            if (!lockedNames.isEmpty())  log.info("@AILocked:  {} — {}", lockedNames.size(),  String.join(", ", lockedNames));
            if (!contextNames.isEmpty()) log.info("@AIContext: {} — {}", contextNames.size(), String.join(", ", contextNames));
            if (!ignoreNames.isEmpty())  log.info("@AIIgnore:  {} — {}", ignoreNames.size(),  String.join(", ", ignoreNames));
            if (!auditNames.isEmpty())   log.info("@AIAudit:   {} — {}", auditNames.size(),   String.join(", ", auditNames));
            if (!draftNames.isEmpty())   log.info("@AIDraft:   {} — {}", draftNames.size(),   String.join(", ", draftNames));
            if (!privacyElements.isEmpty()) log.info("@AIPrivacy: {} — {}", privacyNames.size(), String.join(", ", privacyNames));
            if (!coreNames.isEmpty())    log.info("@AICore:    {} — {}", coreNames.size(),    String.join(", ", coreNames));
            if (!performanceNames.isEmpty()) log.info("@AIPerformance: {} — {}", performanceNames.size(), String.join(", ", performanceNames));
        }

        Map<String, String> contentByService = new LinkedHashMap<>();
        if (activeServices.contains("cursor"))    contentByService.put("cursor",    cursorRules.toString());
        if (activeServices.contains("qwen"))      contentByService.put("qwen",      qwenMd.toString());
        if (activeServices.contains("claude"))    contentByService.put("claude",    claudeMd.toString());
        if (activeServices.contains("gemini"))    contentByService.put("gemini",    geminiMd.toString());
        if (activeServices.contains("codex"))     contentByService.put("codex",     codexAgents.toString());
        if (activeServices.contains("copilot"))   contentByService.put("copilot",   copilot.toString());
        if (activeServices.contains("llms"))      contentByService.put("llms",      llmsTxt.toString());
        if (activeServices.contains("llms_full")) contentByService.put("llms_full", llmsFullTxt.toString());

        if (activeServices.contains("aiexclude") && (activeServices.contains("gemini") || activeServices.contains("codex"))) {
            contentByService.put("aiexclude", aiExclude.toString());
        }

        if (activeServices.contains("cursor_ignore"))  contentByService.put("cursor_ignore",  cursorIgnoreFile.toString());
        if (activeServices.contains("claude_ignore"))  contentByService.put("claude_ignore",  claudeIgnoreFile.toString());
        if (activeServices.contains("copilot_ignore")) contentByService.put("copilot_ignore", copilotIgnoreFile.toString());
        if (activeServices.contains("qwen_ignore"))    contentByService.put("qwen_ignore",    qwenIgnoreFile.toString());

        if (activeServices.contains("codex")) {
            contentByService.put("codex_config", "# " + GENERATED_HEADER.trim() + "\n[project]\nmodel = \"o3-mini\"\napproval_policy = \"on-request\"\n");
            contentByService.put("codex_rules", 
                "# " + GENERATED_HEADER.trim() + "\n# VibeTags: Starlark Command Permissions\n\n" +
                "prefix_rule(\"ls\", \"allow\")\n" +
                "prefix_rule(\"cat\", \"allow\")\n" +
                "prefix_rule(\"grep\", \"allow\")\n" +
                "prefix_rule(\"mvn\", \"prompt\")\n" +
                "prefix_rule(\"npm\", \"prompt\")\n" +
                "prefix_rule(\"git\", \"prompt\")\n" +
                "prefix_rule(\"rm\", \"prompt\")\n");
        }
        if (activeServices.contains("qwen")) {
            contentByService.put("qwen_settings", "{\n  \"project\": {\n    \"model\": \"qwen3-coder-plus\",\n    \"mcp\": {\n      \"enabled\": true\n    }\n  }\n}\n");
            contentByService.put("qwen_refactor", "# /refactor command\n# AUTO-GENERATED BY VIBETAGS\n\nRefactor the current selection to improve maintainability and performance while strictly following the project's contextual rules in QWEN.md.\n");
        }

        if (activeServices.contains("aider_conventions")) contentByService.put("aider_conventions", aiderConventions.toString());
        if (activeServices.contains("aider_ignore"))      contentByService.put("aider_ignore",      aiderIgnore.toString());

        Messager messager = getSafeMessager();
        String genMsg = "VibeTags: Generating files (v" + VERSION + ") for " + contentByService.size() + " active services: " + String.join(", ", contentByService.keySet());
        messager.printMessage(Diagnostic.Kind.NOTE, genMsg);

        contentByService.forEach((service, content) -> {
            Path filePath = serviceFiles.get(service);
            boolean isIgnoreFile = service.endsWith("_ignore") || service.equals("aider_ignore") || service.equals("aiexclude");
            boolean changed = writeFileIfChanged(filePath.toString(), content, collector.anyAnnotationsFound() || isIgnoreFile);
            String relPath = root.relativize(filePath).toString().replace('\\', '/');
            String status = changed ? "updated" : "no changes";
            String fileMsg = "VibeTags: " + status + " - " + relPath;
            getSafeMessager().printMessage(Diagnostic.Kind.NOTE, fileMsg);
            if (log != null) {
                if (changed) log.info("{} — updated", relPath);
                else         log.info("{} — no changes", relPath);
            }
        });

        // Granular Rules Writing — track written qNames so cleanup only removes true orphans
        Set<String> writtenQNames = new java.util.HashSet<>();
        elementRules.forEach((element, builder) -> {
            String qName = element.toString().replace('.', '-').replaceAll("[^a-zA-Z0-9-]", "-");
            writtenQNames.add(qName);
            String simpleName = element.getSimpleName().toString();
            String rulesContent = builder.toString().trim();
            String glob = javax.lang.model.element.ElementKind.PACKAGE.equals(element.getKind())
                ? "**/"+ simpleName + "/**/*.java"
                : "**/"+ simpleName + ".java";

            if (activeServices.contains("cursor_granular")) {
                String mdc = "---\n" +
                    "description: \"AI rules for " + element + "\"\n" +
                    "globs: [\"" + glob + "\"]\n" +
                    "alwaysApply: false\n" +
                    "---\n\n" +
                    "# Rules for " + simpleName + "\n\n" +
                    rulesContent;
                Path path = serviceFiles.get("cursor_granular").resolve(qName + ".mdc");
                writeFileIfChanged(path.toString(), mdc, true);
            }

            if (activeServices.contains("trae_granular")) {
                String md = "---\n" +
                    "alwaysApply: false\n" +
                    "globs: [\"" + glob + "\"]\n" +
                    "description: \"AI rules for " + element + "\"\n" +
                    "---\n\n" +
                    "# Rules for " + simpleName + "\n\n" +
                    rulesContent;
                Path path = serviceFiles.get("trae_granular").resolve(qName + ".md");
                writeFileIfChanged(path.toString(), md, true);
            }

            if (activeServices.contains("roo_granular")) {
                String md = "# Rules for " + simpleName + "\n\n" + rulesContent;
                Path path = serviceFiles.get("roo_granular").resolve(qName + ".md");
                writeFileIfChanged(path.toString(), md, true);
            }
        });

        // Cleanup orphaned granular files (classes that lost their annotations),
        // skipping files we just wrote to avoid a delete-then-recreate cycle.
        if (activeServices.contains("cursor_granular")) cleanupGranularDirectory(serviceFiles.get("cursor_granular"), ".mdc", writtenQNames);
        if (activeServices.contains("trae_granular"))   cleanupGranularDirectory(serviceFiles.get("trae_granular"), ".md", writtenQNames);
        if (activeServices.contains("roo_granular"))    cleanupGranularDirectory(serviceFiles.get("roo_granular"), ".md", writtenQNames);

        checkOrphanedAnnotations(messager, activeServices, 
            !lockedElements.isEmpty(),
            !ignoreElements.isEmpty(), 
            !auditElements.isEmpty());
    }

