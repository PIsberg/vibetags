        claudeIgnoreFile = new StringBuilder("# AUTO-GENERATED BY VIBETAGS\n" + GENERATED_HEADER + "# Claude-specific exclusion list.\n");
        copilotIgnoreFile = new StringBuilder("# AUTO-GENERATED BY VIBETAGS\n" + GENERATED_HEADER + "# Copilot-specific exclusion list.\n");
        qwenIgnoreFile = new StringBuilder("# AUTO-GENERATED BY VIBETAGS\n" + GENERATED_HEADER + "# Qwen-specific exclusion list.\n");

