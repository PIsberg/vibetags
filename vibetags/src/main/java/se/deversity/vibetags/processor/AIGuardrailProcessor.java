package se.deversity.vibetags.processor;

import se.deversity.vibetags.annotations.AIContext;
import se.deversity.vibetags.annotations.AIAudit;
import se.deversity.vibetags.annotations.AIDraft;
import se.deversity.vibetags.annotations.AIIgnore;
import se.deversity.vibetags.annotations.AILocked;

import javax.annotation.processing.*;
import javax.tools.Diagnostic;
import javax.lang.model.SourceVersion;
import javax.lang.model.element.Element;
import javax.lang.model.element.TypeElement;
import java.io.FileWriter;
import java.io.IOException;
import java.io.PrintWriter;
import java.nio.file.Files;
import java.nio.file.Path;
import java.nio.file.Paths;
import java.util.LinkedHashMap;
import java.util.Map;
import java.util.Set;

@SupportedAnnotationTypes("*")
@SupportedSourceVersion(SourceVersion.RELEASE_11)
public class AIGuardrailProcessor extends AbstractProcessor {
    
    private static final String VERSION = "1.0.0-SNAPSHOT";

    // Ensures we process exactly once per compilation run.
    // With @SupportedAnnotationTypes("*") the processor is called in every round,
    // including rounds where no VibeTags annotations are present (e.g. all were removed).
    private boolean processed = false;

    @Override
    public boolean process(Set<? extends TypeElement> annotations, RoundEnvironment roundEnv) {
        if (roundEnv.processingOver() || processed) return false;
        processed = true;

        validateAnnotations(processingEnv.getMessager(), roundEnv);

        String rootPath = Paths.get("").toAbsolutePath().toString();

        String versionHeader = "# Generated by VibeTags v" + VERSION + "\n";

        StringBuilder cursorRules = new StringBuilder("# AUTO-GENERATED AI RULES\n" + versionHeader + "# Do not edit manually.\n\n## LOCKED FILES (DO NOT EDIT)\n");
        StringBuilder claudeMd = new StringBuilder("<!-- " + versionHeader.trim() + " -->\n<project_guardrails>\n  <locked_files>\n");
        StringBuilder aiExclude = new StringBuilder("# AUTO-GENERATED BY VIBETAGS\n" + versionHeader + "# The following files are strictly excluded from AI context.\n");
        StringBuilder codexAgents = new StringBuilder("# AUTO-GENERATED AI RULES\n" + versionHeader + "# Do not edit manually.\n\n## LOCKED FILES (DO NOT EDIT)\n");
        StringBuilder copilot = new StringBuilder("# GitHub Copilot Instructions\n" + versionHeader + "# AUTO-GENERATED BY VIBETAGS. Do not edit manually.\n\n## Locked Files — DO NOT MODIFY\nDo not suggest changes to the following files:\n\n");

        // Standalone ignore files (glob-based)
        StringBuilder cursorIgnoreFile = new StringBuilder("# AUTO-GENERATED BY VIBETAGS\n" + versionHeader + "# Cursor-specific exclusion list.\n");
        StringBuilder claudeIgnoreFile = new StringBuilder("# AUTO-GENERATED BY VIBETAGS\n" + versionHeader + "# Claude-specific exclusion list.\n");
        StringBuilder copilotIgnoreFile = new StringBuilder("# AUTO-GENERATED BY VIBETAGS\n" + versionHeader + "# Copilot-specific exclusion list.\n");

        // Ignore sections for each platform (populated after @AILocked/@AIContext)
        StringBuilder cursorIgnore = new StringBuilder("\n## \uD83D\uDEAB IGNORED ELEMENTS (EXCLUDE FROM CONTEXT)\nDo not reference, suggest changes to, or include the following in completions or answers.\n\n");
        StringBuilder claudeIgnore = new StringBuilder("  <ignored_elements>\n");
        StringBuilder codexIgnore = new StringBuilder("\n## IGNORED ELEMENTS\nThe following elements must be completely excluded from AI context and completions:\n\n");
        StringBuilder geminiIgnore = new StringBuilder("## IGNORED ELEMENTS\nThe following elements must be completely excluded from AI context and completions:\n\n");
        StringBuilder copilotIgnore = new StringBuilder("\n## Ignored Elements\nDo not reference or suggest changes to the following:\n\n");
        
        // Audit sections for each platform
        StringBuilder cursorAudit = new StringBuilder("\n## 🛡️ MANDATORY SECURITY AUDITS\nWhen proposing edits or writing code for the following files, you MUST perform a security review before outputting the final code. You must explicitly state in your response that you have audited the changes for the required vulnerabilities.\n\n");
        StringBuilder codexAudit = new StringBuilder("\n## 🛡️ MANDATORY SECURITY AUDITS\nWhen proposing edits or writing code for the following files, you MUST perform a security review before outputting the final code. You must explicitly state in your response that you have audited the changes for the required vulnerabilities.\n\n");
        StringBuilder copilotAudit = new StringBuilder("\n## Security Audit Requirements\nBefore suggesting changes to the following files, audit for the listed vulnerabilities:\n\n");

        // Process @AILocked
        for (Element element : roundEnv.getElementsAnnotatedWith(AILocked.class)) {
            AILocked locked = element.getAnnotation(AILocked.class);
            String className = element.toString();
            String reason = locked.reason();

            cursorRules.append("* `").append(className).append("` - Reason: ").append(reason).append("\n");

            claudeMd.append("    <file path=\"").append(className).append("\">\n")
                    .append("      <reason>").append(reason).append("</reason>\n")
                    .append("    </file>\n");

            aiExclude.append("**/").append(element.getSimpleName()).append(".java\n");

            codexAgents.append("- **").append(className).append("**: ").append(reason).append("\n");

            copilot.append("- `").append(className).append("` — ").append(reason).append("\n");
        }

        claudeMd.append("  </locked_files>\n  <contextual_instructions>\n");
        cursorRules.append("\n## CONTEXTUAL RULES\n");
        codexAgents.append("\n## CONTEXTUAL RULES\n");
        copilot.append("\n## Contextual Guidelines\n");

        // Process @AIContext
        for (Element element : roundEnv.getElementsAnnotatedWith(AIContext.class)) {
            AIContext context = element.getAnnotation(AIContext.class);
            String className = element.toString();

            cursorRules.append("* `").append(className).append("`\n  * Focus: ").append(context.focus())
                       .append("\n  * Avoid: ").append(context.avoids()).append("\n");

            claudeMd.append("    <file path=\"").append(className).append("\">\n")
                    .append("      <focus>").append(context.focus()).append("</focus>\n")
                    .append("      <avoids>").append(context.avoids()).append("</avoids>\n")
                    .append("    </file>\n");

            codexAgents.append("- `").append(className).append("`: Focus on ").append(context.focus())
                       .append(". Avoid ").append(context.avoids()).append(".\n");

            copilot.append("- `").append(className).append("`\n")
                   .append("  - Focus: ").append(context.focus()).append("\n")
                   .append("  - Avoid: ").append(context.avoids()).append("\n");
        }

        claudeMd.append("  </contextual_instructions>\n");

        // Process @AIIgnore
        boolean hasIgnoreAnnotations = false;
        for (Element element : roundEnv.getElementsAnnotatedWith(AIIgnore.class)) {
            String className = element.toString();

            hasIgnoreAnnotations = true;

            cursorIgnore.append("* `").append(className).append("`\n");

            claudeIgnore.append("    <file path=\"").append(className).append("\"/>\n");

            aiExclude.append("**/").append(element.getSimpleName()).append(".java\n");

            codexIgnore.append("- `").append(className).append("`\n");

            geminiIgnore.append("- `").append(className).append("`\n");

            copilotIgnore.append("- `").append(className).append("`\n");

            String globPattern = "**/"+ element.getSimpleName() + ".java\n";
            cursorIgnoreFile.append(globPattern);
            claudeIgnoreFile.append(globPattern);
            copilotIgnoreFile.append(globPattern);
        }


        // Process @AIAudit
        boolean hasAuditAnnotations = false;
        StringBuilder claudeAudit = new StringBuilder("\n  <audit_requirements>\n");
        StringBuilder geminiMd = new StringBuilder("# GEMINI AI INSTRUCTIONS\n" + versionHeader + "\n## CONTINUOUS AUDIT REQUIREMENTS\n" +
            "You are acting as a Senior Staff Engineer. Whenever you write code for the files listed below, you must ensure your completions and chat responses strictly prevent the listed vulnerabilities:\n\n");
        for (Element element : roundEnv.getElementsAnnotatedWith(AIAudit.class)) {
            AIAudit audit = element.getAnnotation(AIAudit.class);
            String className = element.toString();
            String[] checkFor = audit.checkFor();
            
            if (checkFor.length == 0) continue;
            
            hasAuditAnnotations = true;
            
            // Cursor: Add to mandatory security audits section
            cursorAudit.append("* `").append(className).append("`\n");
            cursorAudit.append("  - Required Checks: ").append(String.join(", ", checkFor)).append("\n");
            
            // Claude: Add XML audit requirements
            claudeAudit.append("    <file path=\"").append(className).append("\">\n");
            for (String vulnerability : checkFor) {
                claudeAudit.append("      <vulnerability_check>").append(vulnerability).append("</vulnerability_check>\n");
            }
            claudeAudit.append("    </file>\n");
            
            // Gemini: Add continuous audit requirements
            geminiMd.append("File: `").append(className).append("`");
            geminiMd.append("\nCritical Vulnerabilities to Prevent: ");
            for (String vulnerability : checkFor) {
                geminiMd.append("\n- ").append(vulnerability);
            }
            geminiMd.append("\n\n");
            
            // Codex: Add mandatory security audits section
            codexAudit.append("* `").append(className).append("`\n");
            codexAudit.append("  - Required Checks: ").append(String.join(", ", checkFor)).append("\n");

            // Copilot: Add security audit requirements
            copilotAudit.append("- `").append(className).append("`\n");
            copilotAudit.append("  - Required Checks: ").append(String.join(", ", checkFor)).append("\n");
        }
        
        // Process @AIDraft
        boolean hasDraftAnnotations = false;
        StringBuilder cursorDraft = new StringBuilder("\n## 📝 IMPLEMENTATION TASKS (TODO)\n" +
            "The following elements are currently in DRAFT mode. Follow the instructions to implement them:\n\n");
        StringBuilder claudeDraft = new StringBuilder("  <implementation_tasks>\n");
        StringBuilder codexDraft = new StringBuilder("\n## IMPLEMENTATION TASKS\nThe following elements are drafts that need implementation:\n\n");
        StringBuilder geminiDraft = new StringBuilder("## IMPLEMENTATION TASKS\nThe following elements are drafts that need implementation:\n\n");
        StringBuilder copilotDraft = new StringBuilder("\n## Implementation Tasks\nFollow these instructions to implement the drafts:\n\n");

        for (Element element : roundEnv.getElementsAnnotatedWith(AIDraft.class)) {
            AIDraft draft = element.getAnnotation(AIDraft.class);
            String className = element.toString();
            String instructions = draft.instructions();
            
            hasDraftAnnotations = true;
            
            cursorDraft.append("* `").append(className).append("` - Task: ").append(instructions).append("\n");
            claudeDraft.append("    <task path=\"").append(className).append("\">\n")
                       .append("      <instructions>").append(instructions).append("</instructions>\n")
                       .append("    </task>\n");
            codexDraft.append("- **").append(className).append("**: ").append(instructions).append("\n");
            geminiDraft.append("- `").append(className).append("`: ").append(instructions).append("\n");
            copilotDraft.append("- `").append(className).append("`: ").append(instructions).append("\n");
        }
        
        // Finalize sections
        if (hasAuditAnnotations) {
            cursorRules.append(cursorAudit);

            claudeAudit.append("  </audit_requirements>\n");
            claudeMd.append(claudeAudit);
            claudeMd.append("\n<rule>\n  If you are asked to modify any file listed in <audit_requirements>, you must first silently analyze your proposed code for the listed <vulnerability_check> items. If your code introduces these vulnerabilities, you must rewrite it before displaying it to the user.\n</rule>\n");

            codexAgents.append(codexAudit);

            copilot.append(copilotAudit);
        }

        if (hasIgnoreAnnotations) {
            cursorRules.append(cursorIgnore);

            claudeIgnore.append("  </ignored_elements>\n");
            claudeMd.append(claudeIgnore);
            claudeMd.append("\n<rule>Never reference or suggest changes to any element listed in <ignored_elements>. Treat these as if they do not exist.</rule>\n");

            codexAgents.append(codexIgnore);

            geminiMd.append(geminiIgnore);

            copilot.append(copilotIgnore);
        }

        if (hasDraftAnnotations) {
            cursorRules.append(cursorDraft);

            claudeDraft.append("  </implementation_tasks>\n");
            claudeMd.append(claudeDraft);

            codexAgents.append(codexDraft);

            geminiMd.append(geminiDraft);

            copilot.append(copilotDraft);
        }
        
        claudeMd.append("</project_guardrails>\n");
        claudeMd.append("\n<rule>Never propose edits to files listed in <locked_files>.</rule>\n");

        // Only regenerate files that already exist — presence on disk is the opt-in signal.
        // If none exist, warn and list what the user can create to opt in.
        Path root = Paths.get(rootPath);
        Map<String, Path> serviceFiles = buildServiceFileMap(root);
        Set<String> activeServices = resolveActiveServices(processingEnv.getMessager(), serviceFiles);

        Map<String, String> contentByService = new LinkedHashMap<>();
        if (activeServices.contains("cursor"))    contentByService.put("cursor",    cursorRules.toString());
        if (activeServices.contains("claude"))    writeFile(rootPath + "/CLAUDE.md", claudeMd.toString());
        
        // .aiexclude is now exclusively for Codex and Gemini
        if (activeServices.contains("aiexclude") && (activeServices.contains("gemini") || activeServices.contains("codex"))) {
            contentByService.put("aiexclude", aiExclude.toString());
        }

        // Standalone ignore files
        if (activeServices.contains("cursor_ignore"))  contentByService.put("cursor_ignore",  cursorIgnoreFile.toString());
        if (activeServices.contains("claude_ignore"))  contentByService.put("claude_ignore",  claudeIgnoreFile.toString());
        if (activeServices.contains("copilot_ignore")) contentByService.put("copilot_ignore", copilotIgnoreFile.toString());

        if (activeServices.contains("codex")) {
            writeFile(rootPath + "/AGENTS.md", codexAgents.toString());
            writeFile(rootPath + "/.codex/config.toml", "# " + versionHeader.trim() + "\n[project]\nmodel = \"o3-mini\"\napproval_policy = \"on-request\"\n");
            writeFile(rootPath + "/.codex/rules/vibetags.rules", 
                "# " + versionHeader.trim() + "\n# VibeTags: Starlark Command Permissions\n\n" +
                "prefix_rule(\"ls\", \"allow\")\n" +
                "prefix_rule(\"cat\", \"allow\")\n" +
                "prefix_rule(\"grep\", \"allow\")\n" +
                "prefix_rule(\"mvn\", \"prompt\")\n" +
                "prefix_rule(\"npm\", \"prompt\")\n" +
                "prefix_rule(\"git\", \"prompt\")\n" +
                "prefix_rule(\"rm\", \"prompt\")\n");
        }
        if (activeServices.contains("gemini"))    writeFile(rootPath + "/gemini_instructions.md", geminiMd.toString());
        if (activeServices.contains("copilot"))   contentByService.put("copilot",   copilot.toString());

        Messager messager = processingEnv.getMessager();
        contentByService.forEach((service, content) -> {
            Path filePath = serviceFiles.get(service);
            boolean changed = writeFileIfChanged(filePath.toString(), content);
            String relPath = root.relativize(filePath).toString().replace('\\', '/');
            String status = changed ? "updated" : "no changes";
            messager.printMessage(Diagnostic.Kind.NOTE, "VibeTags: " + status + " - " + relPath);
        });
        
        // Final check for orphaned annotations (annotations used but their recommended standalone file is missing)
        checkOrphanedAnnotations(messager, activeServices, 
            roundEnv.getElementsAnnotatedWith(AILocked.class).size() > 0,
            hasIgnoreAnnotations, 
            hasAuditAnnotations);

        return false; // do not claim annotations — other processors must still see them
    }

    void validateAnnotations(Messager messager, RoundEnvironment roundEnv) {
        // Contradiction Check: AIDraft + AILocked
        for (Element element : roundEnv.getElementsAnnotatedWith(AILocked.class)) {
            if (element.getAnnotation(AIDraft.class) != null) {
                messager.printMessage(Diagnostic.Kind.WARNING, 
                    "VibeTags: " + element.toString() + " is annotated with both @AIDraft and @AILocked. This is contradictory.", element);
            }
        }
        
        // Empty Audit Check
        for (Element element : roundEnv.getElementsAnnotatedWith(AIAudit.class)) {
            AIAudit audit = element.getAnnotation(AIAudit.class);
            if (audit.checkFor().length == 0) {
                messager.printMessage(Diagnostic.Kind.WARNING, 
                    "VibeTags: @AIAudit on " + element.toString() + " has no 'checkFor' items list. It will be ignored.", element);
            }
        }
    }

    void checkOrphanedAnnotations(Messager messager, Set<String> active, boolean hasLocked, boolean hasIgnore, boolean hasAudit) {
        if (hasIgnore) {
            if (active.contains("cursor") && !active.contains("cursor_ignore")) {
                messager.printMessage(Diagnostic.Kind.WARNING, "VibeTags: @AIIgnore used but .cursorignore is missing for Cursor support. Consider creating it.");
            }
            if (active.contains("claude") && !active.contains("claude_ignore")) {
                messager.printMessage(Diagnostic.Kind.WARNING, "VibeTags: @AIIgnore used but .claudeignore is missing for Claude support. Consider creating it.");
            }
            if (active.contains("copilot") && !active.contains("copilot_ignore")) {
                messager.printMessage(Diagnostic.Kind.WARNING, "VibeTags: @AIIgnore used but .copilotignore is missing for Copilot support. Consider creating it.");
            }
            if ((active.contains("gemini") || active.contains("codex")) && !active.contains("aiexclude")) {
                messager.printMessage(Diagnostic.Kind.WARNING, "VibeTags: @AIIgnore used but .aiexclude is missing for Gemini/Codex support. Consider creating it.");
            }
        }
        
        if (hasLocked) {
            if ((active.contains("gemini") || active.contains("codex")) && !active.contains("aiexclude")) {
                messager.printMessage(Diagnostic.Kind.WARNING, "VibeTags: @AILocked used but .aiexclude (hard guardrail) is missing for Gemini/Codex support. Consider creating it.");
            }
        }
    }

    /**
     * Returns the canonical map of service key → output file path for a given project root.
     */
    static Map<String, Path> buildServiceFileMap(Path root) {
        Map<String, Path> map = new LinkedHashMap<>();
        map.put("cursor",    root.resolve(".cursorrules"));
        map.put("claude",    root.resolve("CLAUDE.md"));
        map.put("aiexclude", root.resolve(".aiexclude"));
        map.put("codex",     root.resolve("AGENTS.md"));
        map.put("gemini",    root.resolve("gemini_instructions.md"));
        map.put("copilot",   root.resolve(".github/copilot-instructions.md"));
        map.put("cursor_ignore",  root.resolve(".cursorignore"));
        map.put("claude_ignore",  root.resolve(".claudeignore"));
        map.put("copilot_ignore", root.resolve(".copilotignore"));
        return map;
    }

    /**
     * Resolves which services should have their files written.
     * Only files that already exist on disk are regenerated — their presence is the opt-in signal.
     * If none of the output files exist, a NOTE is logged listing what the user can create to opt in.
     */
    static Set<String> resolveActiveServices(Messager messager, Map<String, Path> serviceFiles) {
        Set<String> active = serviceFiles.entrySet().stream()
            .filter(e -> Files.exists(e.getValue()))
            .map(Map.Entry::getKey)
            .collect(java.util.stream.Collectors.toSet());

        if (active.isEmpty()) {
            StringBuilder msg = new StringBuilder(
                "VibeTags: No AI config files found - nothing will be generated.\n" +
                "Create one or more of the following files in your project root to opt in:\n");
            serviceFiles.forEach((key, path) ->
                msg.append("  ").append(path.getFileName()).append("\n"));
            messager.printMessage(javax.tools.Diagnostic.Kind.NOTE, msg.toString());
        }

        return active;
    }

    /**
     * Writes {@code content} to {@code path} only if the file's current content differs.
     *
     * @return {@code true} if the file was written (content changed or file was empty),
     *         {@code false} if the existing content was already up-to-date.
     */
    boolean writeFileIfChanged(String path, String content) {
        try {
            Path filePath = Paths.get(path);
            if (Files.exists(filePath)) {
                String existing = Files.readString(filePath, java.nio.charset.StandardCharsets.UTF_8);
                if (existing.strip().equals(content.strip())) {
                    return false;
                }
            }
            try (PrintWriter out = new PrintWriter(new FileWriter(path, java.nio.charset.StandardCharsets.UTF_8))) {
                out.println(content);
            }
            return true;
        } catch (IOException e) {
            processingEnv.getMessager().printMessage(
                javax.tools.Diagnostic.Kind.WARNING, "VibeTags: Failed to write AI rules file: " + path
            );
            return false;
        }
    }

    private void writeFile(String path, String content) {
        Path p = Paths.get(path);
        try {
            Files.createDirectories(p.getParent());
            try (PrintWriter out = new PrintWriter(new FileWriter(path, java.nio.charset.StandardCharsets.UTF_8))) {
                out.println(content);
            }
        } catch (IOException e) {
            processingEnv.getMessager().printMessage(
                javax.tools.Diagnostic.Kind.WARNING, "VibeTags: Failed to write AI rules file: " + path
            );
        }
    }
}
