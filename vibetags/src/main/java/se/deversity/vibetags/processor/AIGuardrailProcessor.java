    @Override
    public synchronized void init(ProcessingEnvironment processingEnv) {
        super.init(processingEnv);
        Map<String, String> options = processingEnv.getOptions();
        
        String rootOverride = options.get("vibetags.root");
        this.root = Paths.get((rootOverride != null && !rootOverride.isBlank())
                ? rootOverride
                : Paths.get("").toAbsolutePath().toString()).toAbsolutePath().normalize();
        
        Messager messager = getSafeMessager();
        String rootMsg = "VibeTags: Root resolved: " + this.root;
        String dirMsg  = "VibeTags: user.dir:      " + System.getProperty("user.dir");
        messager.printMessage(Diagnostic.Kind.NOTE, rootMsg);
        messager.printMessage(Diagnostic.Kind.NOTE, dirMsg);
        
        this.projectName = options.getOrDefault("vibetags.project", "This Project");
        
        String logPath = options.get("vibetags.log.path");
        String logLevel = options.get("vibetags.log.level");
        log = VibeTagsLogger.forRoot(this.root, logPath, logLevel);

        this.fileWriter = new GuardrailFileWriter(GENERATED_HEADER, processingEnv.getMessager(), log);

        // Reset state for potential reuse (especially in tests)
        this.processed = false;
        collector.reset();
        this.elementRules.clear();

        initBuilders();
    }

