package se.deversity.vibetags.processor.internal.content.platforms;

import se.deversity.vibetags.processor.internal.AnnotationCollector;
import se.deversity.vibetags.processor.internal.content.Platform;
import se.deversity.vibetags.processor.internal.content.PlatformRenderer;
import se.deversity.vibetags.processor.internal.content.RenderingContext;

/**
 * PlatformRenderer for generating CodeRabbit's {@code .coderabbit.yaml} review configuration.
 * Emits {@code reviews.path_instructions} so CodeRabbit flags pull requests that violate the
 * VibeTags guardrails during AI review.
 */
public final class CodeRabbitRenderer implements PlatformRenderer {
    @Override
    public String render(AnnotationCollector collector, Platform platform, RenderingContext context) {
        StringBuilder sb = new StringBuilder(1024);
        sb.append("# AUTO-GENERATED BY VIBETAGS\n")
          .append(context.getGeneratedHeader())
          .append("# CodeRabbit AI review configuration. Do not edit manually.\n\n")
          .append("reviews:\n")
          .append("  high_level_summary: true\n")
          .append("  path_instructions:\n")
          .append("    - path: \"**/*.java\"\n")
          .append("      instructions: |\n");

        String block = GuardrailInstructionBlock.build(collector);
        if (block.isBlank()) {
            sb.append("        No VibeTags guardrails are currently declared.\n");
        } else {
            sb.append("        Enforce the following VibeTags guardrails during review. ")
              .append("Flag any change that violates them:\n");
            sb.append(GuardrailInstructionBlock.indent(block.strip(), 8));
        }
        return sb.toString();
    }
}
