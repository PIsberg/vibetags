package se.deversity.vibetags.processor.internal.content.platforms;

import javax.lang.model.element.Element;
import se.deversity.vibetags.processor.internal.AnnotationCollector;
import se.deversity.vibetags.processor.internal.content.FormatterRegistry;
import se.deversity.vibetags.processor.internal.content.Platform;
import se.deversity.vibetags.processor.internal.content.PlatformRenderer;
import se.deversity.vibetags.processor.internal.content.RenderingContext;

/**
 * PlatformRenderer for generating Open Interpreter profiles.
 */
public final class InterpreterRenderer implements PlatformRenderer {
    @Override
    public String render(AnnotationCollector collector, Platform platform, RenderingContext context) {
        StringBuilder sb = new StringBuilder();
        sb.append("# AUTO-GENERATED BY VIBETAGS\n")
          .append(context.getGeneratedHeader())
          .append("# Open Interpreter VibeTags guardrail profile. Do not edit manually.\n\n")
          .append("instructions: |\n");

        StringBuilder rules = new StringBuilder();
        for (Element e : collector.locked()) FormatterRegistry.locked().format(e, rules, Platform.INTERPRETER);
        for (Element e : collector.context()) FormatterRegistry.context().format(e, rules, Platform.INTERPRETER);
        for (Element e : collector.ignore()) FormatterRegistry.ignore().format(e, rules, Platform.INTERPRETER);
        for (Element e : collector.audit()) FormatterRegistry.audit().format(e, rules, Platform.INTERPRETER);
        for (Element e : collector.draft()) FormatterRegistry.draft().format(e, rules, Platform.INTERPRETER);
        for (Element e : collector.privacy()) FormatterRegistry.privacy().format(e, rules, Platform.INTERPRETER);
        for (Element e : collector.core()) FormatterRegistry.core().format(e, rules, Platform.INTERPRETER);
        for (Element e : collector.performance()) FormatterRegistry.performance().format(e, rules, Platform.INTERPRETER);
        for (Element e : collector.contract()) FormatterRegistry.contract().format(e, rules, Platform.INTERPRETER);
        for (Element e : collector.testDriven()) FormatterRegistry.testDriven().format(e, rules, Platform.INTERPRETER);
        for (Element e : collector.threadSafe()) FormatterRegistry.threadSafe().format(e, rules, Platform.INTERPRETER);
        for (Element e : collector.immutable()) FormatterRegistry.immutable().format(e, rules, Platform.INTERPRETER);
        for (Element e : collector.deprecated()) FormatterRegistry.deprecated().format(e, rules, Platform.INTERPRETER);
        for (Element e : collector.observability()) FormatterRegistry.observability().format(e, rules, Platform.INTERPRETER);
        for (Element e : collector.regulation()) FormatterRegistry.regulation().format(e, rules, Platform.INTERPRETER);
        for (Element e : collector.parallelTests()) FormatterRegistry.parallelTests().format(e, rules, Platform.INTERPRETER);
        for (Element e : collector.legacyBridge()) FormatterRegistry.legacyBridge().format(e, rules, Platform.INTERPRETER);
        for (Element e : collector.architecture()) FormatterRegistry.architecture().format(e, rules, Platform.INTERPRETER);
        for (Element e : collector.publicApi()) FormatterRegistry.publicApi().format(e, rules, Platform.INTERPRETER);
        for (Element e : collector.strictExceptions()) FormatterRegistry.strictExceptions().format(e, rules, Platform.INTERPRETER);
        for (Element e : collector.strictTypes()) FormatterRegistry.strictTypes().format(e, rules, Platform.INTERPRETER);
        for (Element e : collector.internationalized()) FormatterRegistry.internationalized().format(e, rules, Platform.INTERPRETER);
        for (Element e : collector.strictClasspath()) FormatterRegistry.strictClasspath().format(e, rules, Platform.INTERPRETER);
        for (Element e : collector.schemaSafe()) FormatterRegistry.schemaSafe().format(e, rules, Platform.INTERPRETER);
        for (Element e : collector.idempotent()) FormatterRegistry.idempotent().format(e, rules, Platform.INTERPRETER);
        for (Element e : collector.featureFlag()) FormatterRegistry.featureFlag().format(e, rules, Platform.INTERPRETER);
        for (Element e : collector.secure()) FormatterRegistry.secure().format(e, rules, Platform.INTERPRETER);

        if (rules.length() > 0) {
            sb.append("  ## Project Guardrails (Generated by VibeTags)\n\n");
            for (String line : rules.toString().split("\n", -1)) {
                sb.append("  ").append(line).append("\n");
            }
        }
        return sb.toString();
    }
}
