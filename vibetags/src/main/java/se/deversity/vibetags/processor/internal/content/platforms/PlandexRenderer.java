package se.deversity.vibetags.processor.internal.content.platforms;

import javax.lang.model.element.Element;
import se.deversity.vibetags.processor.internal.AnnotationCollector;
import se.deversity.vibetags.processor.internal.content.FormatterRegistry;
import se.deversity.vibetags.processor.internal.content.Platform;
import se.deversity.vibetags.processor.internal.content.PlatformRenderer;
import se.deversity.vibetags.processor.internal.content.RenderingContext;

/**
 * PlatformRenderer for generating Plandex config.
 */
public final class PlandexRenderer implements PlatformRenderer {
    @Override
    public String render(AnnotationCollector collector, Platform platform, RenderingContext context) {
        StringBuilder sb = new StringBuilder();
        sb.append("# AUTO-GENERATED BY VIBETAGS\n")
          .append(context.getGeneratedHeader())
          .append("# Plandex AI configuration. Do not edit manually.\n\n")
          .append("guardrails:\n");

        StringBuilder locked = new StringBuilder();
        for (Element e : collector.locked()) FormatterRegistry.locked().format(e, locked, Platform.PLANDEX);
        if (locked.length() > 0) {
            sb.append("  locked:\n").append(locked);
        }

        StringBuilder audit = new StringBuilder();
        for (Element e : collector.audit()) FormatterRegistry.audit().format(e, audit, Platform.PLANDEX);
        if (audit.length() > 0) {
            sb.append("  audit:\n").append(audit);
        }

        StringBuilder privacy = new StringBuilder();
        for (Element e : collector.privacy()) FormatterRegistry.privacy().format(e, privacy, Platform.PLANDEX);
        if (privacy.length() > 0) {
            sb.append("  privacy:\n").append(privacy);
        }

        return sb.toString();
    }
}
