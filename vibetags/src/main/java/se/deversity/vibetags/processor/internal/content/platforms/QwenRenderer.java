package se.deversity.vibetags.processor.internal.content.platforms;

import javax.lang.model.element.Element;
import se.deversity.vibetags.processor.internal.AnnotationCollector;
import se.deversity.vibetags.processor.internal.content.FormatterRegistry;
import se.deversity.vibetags.processor.internal.content.Platform;
import se.deversity.vibetags.processor.internal.content.PlatformRenderer;
import se.deversity.vibetags.processor.internal.content.RenderingContext;

/**
 * PlatformRenderer for generating `QWEN.md`.
 */
public final class QwenRenderer implements PlatformRenderer {
    @Override
    public String render(AnnotationCollector collector, Platform platform, RenderingContext context) {
        if (platform == Platform.QWEN_SETTINGS) {
            return "{\n  \"project\": {\n    \"model\": \"qwen3-coder-plus\",\n    \"mcp\": {\n      \"enabled\": true\n    }\n  }\n}\n";
        }
        if (platform == Platform.QWEN_REFACTOR) {
            return "# /refactor command\n# AUTO-GENERATED BY VIBETAGS\n\nRefactor the current selection to improve maintainability and performance while strictly following the project's contextual rules in QWEN.md.\n";
        }

        StringBuilder sb = new StringBuilder(4096);
        sb.append("# PROJECT CONTEXT\n").append(context.getGeneratedHeader()).append("# AUTO-GENERATED BY VIBETAGS\n\n## LOCKED FILES (DO NOT EDIT)\n");

        for (Element e : collector.locked()) {
            FormatterRegistry.locked().format(e, sb, Platform.QWEN);
        }

        sb.append("\n## CONTEXTUAL RULES\n");
        for (Element e : collector.context()) {
            FormatterRegistry.context().format(e, sb, Platform.QWEN);
        }

        if (!collector.audit().isEmpty()) {
            sb.append("\n## 🛡️ MANDATORY SECURITY AUDITS\nWhen proposing edits or writing code for the following files, you MUST perform a security review. Explicitly state that you have audited the changes for the listed vulnerabilities.\n\n");
            for (Element e : collector.audit()) {
                FormatterRegistry.audit().format(e, sb, Platform.QWEN);
            }
        }

        if (!collector.ignore().isEmpty()) {
            sb.append("\n## IGNORED ELEMENTS\nThe following elements must be completely excluded from AI's memory and context:\n\n");
            for (Element e : collector.ignore()) {
                FormatterRegistry.ignore().format(e, sb, Platform.QWEN);
            }
        }

        if (!collector.draft().isEmpty()) {
            sb.append("\n## IMPLEMENTATION TASKS\nThe following elements are drafts that need implementation:\n\n");
            for (Element e : collector.draft()) {
                FormatterRegistry.draft().format(e, sb, Platform.QWEN);
            }
        }

        if (!collector.privacy().isEmpty()) {
            sb.append("\n## 🔒 PII / PRIVACY GUARDRAILS\nThe following elements handle PII. Never include their runtime values in logs,\nconsole output, external API calls, test fixtures, or mock data.\n\n");
            for (Element e : collector.privacy()) {
                FormatterRegistry.privacy().format(e, sb, Platform.QWEN);
            }
        }

        if (!collector.core().isEmpty()) {
            sb.append("\n## 🧠 CORE FUNCTIONALITY\nThe following elements are well-tested core components. Make changes with extreme caution.\n\n");
            for (Element e : collector.core()) {
                FormatterRegistry.core().format(e, sb, Platform.QWEN);
            }
        }

        if (!collector.performance().isEmpty()) {
            sb.append("\n## ⚡ PERFORMANCE CONSTRAINTS\nHot-path elements — O(n²) complexity is forbidden. Reason about complexity before proposing changes.\n\n");
            for (Element e : collector.performance()) {
                FormatterRegistry.performance().format(e, sb, Platform.QWEN);
            }
        }

        if (!collector.contract().isEmpty()) {
            sb.append("\n## 🔐 CONTRACT-FROZEN SIGNATURES\nSignatures (method names, parameters, return types, checked exceptions) are immutable. Internal logic may be changed.\n\n");
            for (Element e : collector.contract()) {
                FormatterRegistry.contract().format(e, sb, Platform.QWEN);
            }
        }

        if (!collector.testDriven().isEmpty()) {
            sb.append("\n## 🧪 TEST-DRIVEN REQUIREMENTS\nChanges to the following elements MUST be accompanied by a matching test update in the same response.\n\n");
            for (Element e : collector.testDriven()) {
                FormatterRegistry.testDriven().format(e, sb, Platform.QWEN);
            }
        }

        if (!collector.threadSafe().isEmpty()) {
            sb.append("\n## 🧵 THREAD-SAFE BY DESIGN\nThese elements are explicitly designed to be thread-safe — preserve the synchronization invariant.\n\n");
            for (Element e : collector.threadSafe()) {
                FormatterRegistry.threadSafe().format(e, sb, Platform.QWEN);
            }
        }

        if (!collector.immutable().isEmpty()) {
            sb.append("\n## ❄️ IMMUTABLE TYPES\nThe following types are immutable — never add setters, mutating methods, or non-final fields.\n\n");
            for (Element e : collector.immutable()) {
                FormatterRegistry.immutable().format(e, sb, Platform.QWEN);
            }
        }

        if (!collector.deprecated().isEmpty()) {
            sb.append("\n## ⚠️ DEPRECATED ELEMENTS\nDo not extend or build on these elements — migrate callers to the replacement.\n\n");
            for (Element e : collector.deprecated()) {
                FormatterRegistry.deprecated().format(e, sb, Platform.QWEN);
            }
        }

        if (!collector.observability().isEmpty()) {
            sb.append("\n## 📡 OBSERVABILITY INSTRUMENTATION\nThese elements carry instrumentation watched by dashboards. Preserve every metric, trace, and log statement.\n\n");
            for (Element e : collector.observability()) {
                FormatterRegistry.observability().format(e, sb, Platform.QWEN);
            }
        }

        if (!collector.regulation().isEmpty()) {
            sb.append("\n## 📜 REGULATORY COMPLIANCE\nThese elements implement compliance clauses — document compliance impact and never weaken the requirement.\n\n");
            for (Element e : collector.regulation()) {
                FormatterRegistry.regulation().format(e, sb, Platform.QWEN);
            }
        }

        if (!collector.parallelTests().isEmpty()) {
            sb.append("\n## 🧪 STRICT TEST ISOLATION\nTests must run in complete thread isolation without shared mutable state:\n\n");
            for (Element e : collector.parallelTests()) {
                FormatterRegistry.parallelTests().format(e, sb, Platform.QWEN);
            }
        }

        if (!collector.legacyBridge().isEmpty()) {
            sb.append("\n## 🌉 LEGACY COMPATIBILITY BRIDGE\nModernization/structural refactoring is prohibited for these elements:\n\n");
            for (Element e : collector.legacyBridge()) {
                FormatterRegistry.legacyBridge().format(e, sb, Platform.QWEN);
            }
        }

        if (!collector.architecture().isEmpty()) {
            sb.append("\n## 🏛️ ARCHITECTURAL BOUNDARY CONSTRAINTS\nStrict layered architecture constraints apply to these elements:\n\n");
            for (Element e : collector.architecture()) {
                FormatterRegistry.architecture().format(e, sb, Platform.QWEN);
            }
        }

        if (!collector.publicApi().isEmpty()) {
            sb.append("\n## 🔌 PUBLIC API SURFACE PROTECTION\nPublic API surface. Signatures and backwards compatibility must be strictly preserved:\n\n");
            for (Element e : collector.publicApi()) {
                FormatterRegistry.publicApi().format(e, sb, Platform.QWEN);
            }
        }

        if (!collector.strictExceptions().isEmpty()) {
            sb.append("\n## 🚨 STRICT EXCEPTION HANDLING\nGeneric exception throwing/catching is strictly prohibited for these elements:\n\n");
            for (Element e : collector.strictExceptions()) {
                FormatterRegistry.strictExceptions().format(e, sb, Platform.QWEN);
            }
        }

        if (!collector.strictTypes().isEmpty()) {
            sb.append("\n## 🏷️ STRICT TYPE SAFETY\nLoose typing is strictly prohibited for these elements:\n\n");
            for (Element e : collector.strictTypes()) {
                FormatterRegistry.strictTypes().format(e, sb, Platform.QWEN);
            }
        }

        if (!collector.internationalized().isEmpty()) {
            sb.append("\n## 🌐 INTERNATIONALIZATION MANDATE\nHardcoding user-facing strings is strictly prohibited for these elements:\n\n");
            for (Element e : collector.internationalized()) {
                FormatterRegistry.internationalized().format(e, sb, Platform.QWEN);
            }
        }

        if (!collector.strictClasspath().isEmpty()) {
            sb.append("\n## 🛡️ STRICT CLASSPATH INTEGRITY\nDynamic runtime class loading is strictly prohibited for these elements:\n\n");
            for (Element e : collector.strictClasspath()) {
                FormatterRegistry.strictClasspath().format(e, sb, Platform.QWEN);
            }
        }

        if (!collector.schemaSafe().isEmpty()) {
            sb.append("\n## 🗄️ SCHEMA & SERIALIZATION SAFETY\nModifying schema or data formats without explicit migration plans is prohibited:\n\n");
            for (Element e : collector.schemaSafe()) {
                FormatterRegistry.schemaSafe().format(e, sb, Platform.QWEN);
            }
        }

        if (!collector.idempotent().isEmpty()) {
            sb.append("\n## ♻️ IDEMPOTENCY GUARANTEES\nMultiple invocations of these operations must produce the same result:\n\n");
            for (Element e : collector.idempotent()) {
                FormatterRegistry.idempotent().format(e, sb, Platform.QWEN);
            }
        }

        if (!collector.featureFlag().isEmpty()) {
            sb.append("\n## 🚩 FEATURE FLAG GATED CODE\nThese elements are gated behind a feature flag. Never assume it is always active:\n\n");
            for (Element e : collector.featureFlag()) {
                FormatterRegistry.featureFlag().format(e, sb, Platform.QWEN);
            }
        }

        if (!collector.secure().isEmpty()) {
            sb.append("\n## 🔐 SECURITY-CRITICAL CODE\nNever weaken security properties of these elements. Flag any change for security review:\n\n");
            for (Element e : collector.secure()) {
                FormatterRegistry.secure().format(e, sb, Platform.QWEN);
            }
        }

        return sb.toString();
    }
}
