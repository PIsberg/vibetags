package se.deversity.vibetags.processor.internal.content.platforms;

import se.deversity.vibetags.processor.internal.AnnotationCollector;
import se.deversity.vibetags.processor.internal.content.Platform;
import se.deversity.vibetags.processor.internal.content.PlatformRenderer;
import se.deversity.vibetags.processor.internal.content.RenderingContext;

/**
 * PlatformRenderer for generating Roo Code's {@code .roomodes} custom-mode file (YAML).
 * Defines a dedicated "VibeTags Architect" mode whose custom instructions carry the project's
 * guardrails, constraining the agent to structural changes that respect every VibeTags rule.
 */
public final class RooModesRenderer implements PlatformRenderer {
    @Override
    public String render(AnnotationCollector collector, Platform platform, RenderingContext context) {
        StringBuilder sb = new StringBuilder(1024);
        sb.append("# AUTO-GENERATED BY VIBETAGS\n")
          .append(context.getGeneratedHeader())
          .append("# Roo Code custom modes. Do not edit manually.\n\n")
          .append("customModes:\n")
          .append("  - slug: vibetags-architect\n")
          .append("    name: 🛡️ VibeTags Architect\n")
          .append("    roleDefinition: >-\n")
          .append("      You are a structural architect that strictly respects VibeTags guardrails.\n")
          .append("      Before modifying any code, read its VibeTags annotations and never violate\n")
          .append("      a locked, contract-frozen, core, or privacy-protected element.\n")
          .append("    groups:\n")
          .append("      - read\n")
          .append("      - edit\n")
          .append("    customInstructions: |-\n");

        String block = GuardrailInstructionBlock.build(collector).strip();
        if (block.isBlank()) {
            sb.append("      No VibeTags guardrails are currently declared.\n");
        } else {
            sb.append("      Enforce the following VibeTags guardrails at all times:\n");
            sb.append(GuardrailInstructionBlock.indent(block.strip(), 6));
        }
        return sb.toString();
    }
}
