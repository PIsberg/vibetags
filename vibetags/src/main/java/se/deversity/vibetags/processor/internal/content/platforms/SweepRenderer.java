package se.deversity.vibetags.processor.internal.content.platforms;

import javax.lang.model.element.Element;
import se.deversity.vibetags.processor.internal.AnnotationCollector;
import se.deversity.vibetags.processor.internal.content.FormatterRegistry;
import se.deversity.vibetags.processor.internal.content.Platform;
import se.deversity.vibetags.processor.internal.content.PlatformRenderer;
import se.deversity.vibetags.processor.internal.content.RenderingContext;

/**
 * PlatformRenderer for generating Sweep config.
 */
public final class SweepRenderer implements PlatformRenderer {
    @Override
    public String render(AnnotationCollector collector, Platform platform, RenderingContext context) {
        StringBuilder sb = new StringBuilder();
        sb.append("# AUTO-GENERATED BY VIBETAGS\n")
          .append(context.getGeneratedHeader())
          .append("# Sweep AI code review rules. Do not edit manually.\n\n")
          .append("rules:\n");

        StringBuilder rules = new StringBuilder();
        for (Element e : collector.locked()) FormatterRegistry.locked().format(e, rules, Platform.SWEEP);
        for (Element e : collector.audit()) FormatterRegistry.audit().format(e, rules, Platform.SWEEP);
        for (Element e : collector.draft()) FormatterRegistry.draft().format(e, rules, Platform.SWEEP);
        for (Element e : collector.privacy()) FormatterRegistry.privacy().format(e, rules, Platform.SWEEP);
        for (Element e : collector.core()) FormatterRegistry.core().format(e, rules, Platform.SWEEP);
        for (Element e : collector.performance()) FormatterRegistry.performance().format(e, rules, Platform.SWEEP);
        for (Element e : collector.contract()) FormatterRegistry.contract().format(e, rules, Platform.SWEEP);
        for (Element e : collector.testDriven()) FormatterRegistry.testDriven().format(e, rules, Platform.SWEEP);
        for (Element e : collector.idempotent()) FormatterRegistry.idempotent().format(e, rules, Platform.SWEEP);
        for (Element e : collector.featureFlag()) FormatterRegistry.featureFlag().format(e, rules, Platform.SWEEP);
        for (Element e : collector.secure()) FormatterRegistry.secure().format(e, rules, Platform.SWEEP);

        if (rules.length() > 0) {
            sb.append(rules);
        } else {
            sb.append("  []\n");
        }
        return sb.toString();
    }
}
